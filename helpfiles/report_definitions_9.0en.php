	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="9.0">9.0
	Quality of Care: Care Reminders</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">This
	category includes reports to be used as care reminders for specific
	patients at the selected clinic site.  For example, one of the
	reports lists patients that are medically eligible for ART but have
	not been enrolled.  This report is a rough indicator of quality of
	care, since faster enrollment is not necessarily equal to better
	care.  There are legitimate reasons to delay initiation of ART, such
	as lack of preparation for patient adherence or need to initiate TB
	treatment before starting ART.  Nevertheless, this report gives a
	tool to begin to analyze patterns in ART enrollment delay for
	possible improvement in service delivery.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
