	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="23.0">23.0
	Medical Summary</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">This
	report can be used to generate a clinical summary of a patient's
	care history within the clinic site.  It is meant to be used as a
	backup of the electronic medical record if paper forms are no longer
	being used at the clinic site.  The summary includes most of the
	data that exists in the system for a particular patient including
	medication history, known allergies, visit history, lab test
	results, etc.  By default, this report is generated as a PDF
	document so that it will be easier to print from within the EMR.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="23.1">23.1
	Usage</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">To
	use this report, simply click on the link titled 'Medical Summary
	(pdf)' that appears in the banner after a patient has been selected.
	 The report parameters page does not apply to this report.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
