	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">This
	report covers all patients regardless of activity and treatment
	status and can be used to give a quick overview of all the
	demographic information stored in the system.  It will display a set
	of results that show clinic patient ID, name, date of birth,
	national ID, gender and mother's first name.  A patient's gender and
	mother's first name can be reassigned using this report by entering
	the new value in the form under the columns titled 'Reassign gender'
	and 'Mother's First Name' and then clicking the 'Save' button.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
