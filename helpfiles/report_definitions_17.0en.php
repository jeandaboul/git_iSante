	Figure 19: Results header for the 'Patients with no gender
	designated' report</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">There
	are two action buttons at the top of the report results.  The
	'Close' button will close the report results window (or tab).  The
	'excel' button will generate an alternate version of the same report
	results in CSV format.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">Below
	the two action buttons is the results header.  Each of the columns
	can be sorted in ascending or descending order by clicking on the
	down arrow (left side) or up arrow (right side) next to the column
	title.</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="17.3">17.3
	Relevant Definitions</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_6.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#6.3">6.3 Relevant Definitions</A> above for definitions of the
	columns titled 'Clinic Patient ID', 'National ID', 'First Name' and
	'Last Name')</I></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="17.3.1">17.3.1
	Patients with no gender specified</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 Patients with no gender indicated.  Gender is a required variable
	for PEPFAR monthly facility reports.  This data is derived from the
	absence of a selected option on the patient's registration form in
	the section titled 'Demographic Information' for the field titled
	'Sex'.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="17.3.2">17.3.2
	Patients without birth year or age specified</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 Patients without a valid birth year (2 digit) or age entered. 
	Birth year and age are used for calculating the patient&rsquo;s
	current age, which is a required variable for PEPFAR monthly
	facility reports.  This data is derived from the absence </SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">of
	any values entered on the patient's registration form in the section
	titled 'Demographic Information' for the fields titled 'Date of
	Birth' and 'If date unknown, age in years'.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="17.3.3">17.3.3
	Patients with no clinic number assigned</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 Patients without a valid clinic patient ID entered.  This data is
	derived from the absence of a value entered on the patient's
	registration form in the section titled 'Demographic Information'
	for the field titled 'Clinic ID'.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="17.3.4">17.3.4
	Active patients with prescription forms completed</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 Any new, active or at risk patients with a completed prescription
	form </SPAN></FONT><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_4.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#4.0">4.0 Classification of Patients by Activity Status</A> above for
	the definitions of patient statuses)</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">.
	 This report can be used at the sites to cross-check paper-based
	pharmacy logs (notebooks) with a patient's electronic records, and
	can help pharmacy personnel update the EMR system with complete
	pharmacy order and dispensing information for all patients.  This
	data is derived from any completed prescription forms for the
	selected clinic site.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="17.3.5">17.3.5
	Active patients with lab forms completed</A></I></FONT><I>:</I>  Any
	new, active or at risk patients with a completed laboratory form
	<I>(see section <A HREF="helpfile.php?file=report_definitions_4.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#4.0">4.0 Classification of Patients by Activity Status</A>
	above for the definitions of patient statuses)</I>.  This report can
	be used at the sites to cross-check paper-based laboratory logs
	(notebooks) with a patient's electronic records, and can help
	laboratory personnel update the EMR system with complete laboratory
	order and fulfillment information for all patients.  This data is
	derived from any completed laboratory forms for the selected clinic
	site.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
