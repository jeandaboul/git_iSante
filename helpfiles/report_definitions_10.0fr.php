	Qualit&eacute; des soins&nbsp;:&nbsp;&Eacute;ligible pour un
        traitement TB mais non d&eacute;marr&eacute;</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">
        Cette
cat&eacute;gorie comprend 4 rapports &agrave; utiliser comme rappel
de traitement TB pour des patients sp&eacute;cifiques au sein d&rsquo;un
&eacute;tablissement. Trois rapports listent les patients qui
remplissent certains crit&egrave;res li&eacute;s &agrave; la TB mais
qui n&rsquo;ont pas encore re&ccedil;u de traitement contre la TB. Le
quatri&egrave;me rapport affiche une liste de patients dont le
traitement TB est termin&eacute;.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="10.1">10.1
	Utilisation</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Afin
d&rsquo;utiliser ces rapports, veuillez simplement s&eacute;lectionner
un ou plusieurs type(s) de statut de patient dans la section du
rapport &laquo;&nbsp;page de param&egrave;tres &raquo;, puis
s&eacute;lectionner une p&eacute;riode de temps, et tout autre
crit&egrave;re &agrave; utiliser sur cette page, afin de g&eacute;n&eacute;rer
les r&eacute;sultats de rapport souhait&eacute;s
	<I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_5.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#5.0">5.0 &laquo;&nbsp;Page de param&egrave;tres de rapport&nbsp;&raquo;</A>, ci-dessus pour les d&eacute;finitions des sections de la page de param&egrave;tres du rapport)</I>. Cliquez ensuite sur le bouton
&laquo;&nbsp;Soumettre&nbsp;&raquo;.</FONT></P>
