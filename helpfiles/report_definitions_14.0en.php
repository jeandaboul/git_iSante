	 The total number of patients at the clinic site at the end of
	the analysis period.  All activity statuses, except discontinued, are counted in
	this total </SPAN></FONT><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_4.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#4.0">4.0 Classification of Patients by Activity Status</A> above for
	the definitions of patient statuses)</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><FONT SIZE=3><I><A NAME="14.3.3.2">14.3.3.2
	Patient Retention in ART Care</A></I></FONT></FONT><I>:</I>
	 The denominator for this indicator is defined as 'Cumulative number of HIV+ patients ever enrolled on ART excluding discontinued patients and pediatric patients with a negative PCR result'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">The
	numerator for this indicator is defined as 'Cumulative number of HIV+ patients ever enrolled on ART with at least one visit during the last 3 months'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="14.3.3.3">14.3.3.3
	Patient Retention in Clinical Care</A></I></FONT><I>:</I>
	 The denominator for this indicator is defined as 'Cumulative number of HIV+ patients not enrolled on ART care excluding discontinued patients and pediatric patients with a negative PCR result'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">The
	numerator for this indicator is defined as 'Cumulative number of HIV+ patients not enrolled on ART treatment with at least one visit in the last 6 months'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="14.3.3.4">14.3.3.4
	CD4 Monitoring</A></I></FONT><I>:</I>
	 The denominator for this indicator is defined as 'Number of HIV+ patients ever enrolled at the site more than six months prior to the period of analysis, excluding discontinued patients and pediatric patients with a negative PCR result'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<FONT FACE="Arial, sans-serif">The numerator for this
	indicator is defined as 'Number of HIV+ patients ever enrolled at the site more than six months prior to the period of analysis who have had 2 CD4 count in last 8 months'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="14.3.3.5">14.3.3.5
	CD4 at Enrollment</A></I></FONT><I>:</I>
	 The denominator for this indicator is defined as 'Number of HIV+ patients enrolled during the reported period excluding discontinued patients and pediatric patients with a negative PCR result'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="background: transparent">The
	numerator for this indicator is defined as 'Number of HIV+ patients enrolled during the reported period who have benefited from a CD4 count at enrollment'.</SPAN></SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="14.3.3.6">14.3.3.6
	ART Enrollment</A></I></FONT><I>:</I>
	 The denominator for this indicator is defined as 'Number of HIV+ patients eligible (newly or old who never received ART before the analysis period) for ARVs as defined by the MSPP in analysis period excluding discontinued patients and pediatric patients with a negative PCR result'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="background: transparent">The
	numerator for this indicator is defined as 'Number of HIV+ patients enrolled on ART by the standards during the analysis period'.</SPAN></SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="14.3.3.7">14.3.3.7
	Cotrimoxazole Prophylaxis</A></I></FONT><I>:</I>
	 The denominator for this indicator is defined as 'Number of HIV+ patients with at least one medical visit during the analysis period, excluding discontinued patients and pediatric patients with a negative PCR result'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="background: transparent">The
	numerator for this indicator is defined as 'Number of HIV+ patients given cotrimoxazole prophylaxis during the analysis period'.</SPAN></SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="14.3.3.8">14.3.3.8
	Adherence Assessment</A></I></FONT><I>:</I>
	 The denominator for this indicator is defined as 'Number of HIV+ patients on ART with at least one medical visit during the last 3 months, excluding discontinued patients and pediatric patients with a negative PCR result'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<FONT FACE="Arial, sans-serif"><SPAN STYLE="background: transparent">The
	numerator for this indicator is defined as 'Number of ART patients who have been assessed by a pill count or completing the questionnaire during the last 3 months'.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="14.3.3.9">14.3.3.9
	Level of Adherence</A></I></FONT><I>:</I>
	 The denominator for this indicator is defined as 'Cumulative number of HIV+ patients enrolled on ART for more than 3 months who underwent evaluation of their adherence, excluding discontinued patients and pediatric patients with a negative PCR result'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">The
	numerator for this indicator is defined as 'Cumulative number of HIV+ patients enrolled on ART for more than three months with a level of adherence to ARV treatment greater than or equal to 95%'.</SPAN></SPAN></SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="14.3.3.10">14.3.3.10
	TB Screening</A></I></FONT><I>:</I>
	 The denominator for this indicator is defined as 'Number of HIV+ patients newly enrolled in care during the analysis period, excluding discontinued patients and pediatric patients with a negative PCR result'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">The
	numerator for this indicator is defined as 'Number of HIV+ patients evaluated for TB at enrollment during the analysis period'.</SPAN></SPAN></SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<BR>
	</P>
