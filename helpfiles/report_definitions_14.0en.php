	numerator for this indicator is defined as 'Number of HIV+ patients who underwent a nutritional assessment during the analysis period'.</SPAN></SPAN></SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<BR>
	</P>
