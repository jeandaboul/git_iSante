	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">The
	numerator for this indicator is defined as 'Number of HIV+ adult patients with BMI less than or equal to 16 or HIV+ pediatric patients with severe malnutrition according to guidelines during the analysis period'.</SPAN></SPAN></SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="14.3.3.14">14.3.3.14
	Family Planning</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The denominator for this indicator is defined as 'Number of HIV+ women of childbearing age who received at least one visit during the analysis period, excluding discontinued patients and pregnant women'.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">The
	numerator for this indicator is defined as 'Number of HIV+ women of childbearing age having accepted a FP method during the analysis period'.</SPAN></SPAN></SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium">
	<BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="14.3.3.15">14.3.3.15
