	The default length of the period of analysis for this report is six months.
        If a shorter or longer period is required, simply enter the desired
        interval in months on the report parameters page.
        This report runs in intervals computed backwards
	from the end date specified.  If you select a start date and end
	date that covers less than the selected interval period, no results will
	be displayed.  If you select a start date, end date and interval that covers
	more than one interval time period, the report will produce results
	for each complete interval in the period specified.  For
