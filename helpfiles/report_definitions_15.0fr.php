</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="15.0">15.0
	Gestion de programme&nbsp;:&nbsp;D&eacute;mographie des patients</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">
        Ces
rapports englobent tous les patients, quelque soit leur statut
d&rsquo;activit&eacute; et de traitement et son destin&eacute;s &agrave;
produire des r&eacute;sultats agr&eacute;g&eacute;s dans diverses
cat&eacute;gories d&eacute;mographiques pour la population enti&egrave;re
d&rsquo;un &eacute;tablissement m&eacute;dical.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="15.1">15.1
	Utilisation</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Afin
d&rsquo;utiliser ce rapport, veuillez simplement s&eacute;lectionner
une p&eacute;riode de temps, afin de g&eacute;n&eacute;rer les
r&eacute;sultats de rapport souhait&eacute;s
	<I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_5.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#5.0">5.0 &laquo;&nbsp;Page de param&egrave;tres de rapport&nbsp;&raquo;</A>, ci-dessus pour les d&eacute;finitions des sections de la page de param&egrave;tres du rapport)</I></FONT><FONT FACE="Arial, sans-serif">.
	 Cliquez ensuite sur le bouton &laquo;&nbsp;Soumettre&nbsp;&raquo;.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><B>Note&nbsp;:&nbsp;</B></FONT><FONT FACE="Arial, sans-serif">
        Seulement
les champs &laquo;&nbsp;Date de d&eacute;but&nbsp;&raquo; et &laquo;&nbsp;Date
de fin&nbsp;&raquo; peuvent &ecirc;tre s&eacute;lectionn&eacute;s sur
ces rapports puisque ces rapports sont suppos&eacute;s couvrir tous
les patients des &eacute;tablissements qui effectuent le rapport.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="15.2">15.2
	R&eacute;sultats</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Les
r&eacute;sultats du rapport apparaissent dans une nouvelle fen&ecirc;tre
de navigateur ou un nouvel onglet, suivant la mani&egrave;re dont le
navigateur est configur&eacute;. Un exemple du rapport de la
d&eacute;mographie des patients est illustr&eacute; par la figure 17,
dans la section
	<A HREF="#15.2.1">15.2.1</A> </FONT><FONT FACE="Arial, sans-serif"><I>(ci-dessous)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="15.2.1"><IMG SRC="images/report_definitions_html_fr_376745aa.jpg" NAME="graphics17" WIDTH=625 HEIGHT=298 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>15.2.1
	Figure 17 : Exemple de rapport &laquo;&nbsp;&Acirc;ge &agrave; la premi&egrave;re visite&nbsp;&raquo;</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Il
existe 2 boutons de commande au dessus du rapport de r&eacute;sultats.
Le bouton &laquo;&nbsp;Fermer&nbsp;&raquo;
ferme la fen&ecirc;tre de r&eacute;sultats (onglet). Le bouton
&laquo;&nbsp;excel&nbsp;&raquo; g&eacute;n&egrave;re une version
alternative du m&ecirc;me rapport de r&eacute;sultats sous le format
CSV.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Le
r&eacute;capitulatif des param&egrave;tres, se trouve sous les deux
boutons de commande. Chacune des colonnes peut &ecirc;tre tri&eacute;e
par ordre croissant ou d&eacute;croissant, en cliquant sur la fl&egrave;che
descendante (&agrave; gauche) ou la fl&egrave;che ascendante (&agrave;
droite), au niveau du titre de colonne.
	</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="15.3">15.3
	D&eacute;finitions importantes</A></B></FONT></FONT><BR><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="15.3.1">15.3.1
	Nombre de patients</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le
nombre total de patients non discontinu&eacute;s, quelque soit leur
statut d&rsquo;activit&eacute;
	<I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_4.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#4.0">4.0 &laquo;&nbsp;Classification des patients par statut d&rsquo;activit&eacute;&nbsp;&raquo;</A>
ci-dessus pour les d&eacute;finitions des statuts de patient)</I>
        pour les p&eacute;riodes
s&eacute;lectionn&eacute;es. Ces donn&eacute;es proviennent de toutes
les fiches d&rsquo;enregistrement et saisies dans l&rsquo;EMR pour
l&rsquo;&eacute;tablissement m&eacute;dical sp&eacute;cifi&eacute;.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="15.3.2">15.3.2
	Patients par groupe d&rsquo;&acirc;ge</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le nombre total de patients non discontinu&eacute;s divis&eacute;s
par tranches d&rsquo;&acirc;ges (0-17, 18-20, 21-30, 31-40, 41-50,
51-60, 61-70, 71-80 et inconnu) lors de la p&eacute;riode sp&eacute;cifi&eacute;e.
Ces donn&eacute;es proviennent des fiches d&rsquo;enregistrement
compl&eacute;t&eacute;es au sein de l&rsquo;&eacute;tablissement
m&eacute;dical sp&eacute;cifi&eacute;. Les &acirc;ges sont calcul&eacute;s
&agrave; partir des champs intitul&eacute;s &laquo;&nbsp;Date de
naissance&nbsp;&raquo; ou &laquo;&nbsp;Si date inconnue, &acirc;ge en
ann&eacute;es&nbsp;&raquo; sur la fiche de
premi&egrave;re visite du patient.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="15.3.3">15.3.3
	&Acirc;ge &agrave; la premi&egrave;re visite</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le
nombre total de patients non discontinu&eacute;s divis&eacute;s par
tranches d&rsquo;&acirc;ges (0-17, 18-20, 21-30, 31-40, 41-50, 51-60,
61-70, 71-80 et inconnu) lors de leur premi&egrave;re visite. Ces
donn&eacute;es proviennent des fiches d&rsquo;enregistrement
compl&eacute;t&eacute;es au sein de l&rsquo;&eacute;tablissement
m&eacute;dical sp&eacute;cifi&eacute;. Les &acirc;ges sont calcul&eacute;s
&agrave; partir des champs intitul&eacute;s &laquo;&nbsp;Date de
naissance&nbsp;&raquo; ou &laquo;&nbsp;Si date inconnue, &acirc;ge en
ann&eacute;es&nbsp;&raquo; sur la fiche de premi&egrave;re visite du patient.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="15.3.4">15.3.4
	Nombre de patients par sexe</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
