sexe (homme et femme). Ces donn&eacute;es proviennent des fiches
d&rsquo;enregistrement compl&eacute;t&eacute;es au sein de
l&rsquo;&eacute;tablissement m&eacute;dical sp&eacute;cifi&eacute;.
Le sexe provident du champ intitul&eacute; &laquo;&nbsp;Sexe&nbsp;&raquo;
sur la fiche de premi&egrave;re visite du patient.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="15.3.5">15.3.5
