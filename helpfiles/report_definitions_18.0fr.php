</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="18.0">18.0
	Qualit&eacute; des donn&eacute;es&nbsp;:&nbsp;Donn&eacute;es non-valides</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Ces
rapports englobent tous les patients, quelque soit leur statut
d&rsquo;activit&eacute; et de traitement et sont destin&eacute;s &agrave;
produire des r&eacute;sultats individuels dans les diverses
cat&eacute;gories o&ugrave; des donn&eacute;es sont non valides sur
des fiches sp&eacute;cifiques de patient.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="18.1">18.1
	Utilisation</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Afin
d&rsquo;utiliser ces rapports, veuillez simplement cliquer sur le
lien du titre de rapport. La page de param&egrave;tres de rapport ne
s&rsquo;applique pas &agrave; ces rapports.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="18.2">18.2
	R&eacute;sultats</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Les
r&eacute;sultats du rapport apparaissent dans une nouvelle fen&ecirc;tre
de navigateur ou un nouvel onglet, suivant la mani&egrave;re dont le
navigateur est configur&eacute;. Un exemple de l&rsquo;en-t&ecirc;te
des r&eacute;sultats est illustr&eacute; par la figure 20, dans la
section
	<A HREF="#18.2.1">18.2.1</A> </FONT><FONT FACE="Arial, sans-serif"><I>(ci-dessous)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="#18.2.1"><IMG SRC="images/report_definitions_html_fr_m62cfe464.jpg" NAME="graphics20" ALIGN=BOTTOM WIDTH=800 HEIGHT=81 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>18.2.1
	Figure 20 : En-t&ecirc;te de r&eacute;sultats pour le rapport &laquo;&nbsp;Date de visite ult&eacute;rieure &agrave; la date d'entr&eacute;e des donn&eacute;es&nbsp;&raquo;</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Il
existe 2 boutons de commande au dessus du rapport de r&eacute;sultats.
Le bouton &laquo;&nbsp;Fermer&nbsp;&raquo;
ferme la fen&ecirc;tre de r&eacute;sultats (onglet). Le bouton
'excel' g&eacute;n&egrave;re une version alternative du m&ecirc;me
rapport de r&eacute;sultats sous le format CSV.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Le
r&eacute;capitulatif des param&egrave;tres, se trouve sous les deux
boutons de commande. Chacune des colonnes peut &ecirc;tre tri&eacute;e
par ordre croissant ou d&eacute;croissant, en cliquant sur la fl&egrave;che
descendante (&agrave; gauche) ou la fl&egrave;che ascendante (&agrave;
droite), au niveau du titre de colonne.
	</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="18.3">18.3
	D&eacute;finitions importantes</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_6.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#6.3">6.3 &laquo;&nbsp;D&eacute;finitions importantes&nbsp;&raquo;</A> 
	ci-dessus pour les d&eacute;finitions des colonnes intitul&eacute;es
&laquo;&nbsp;No. de patient attribu&eacute; par le site&nbsp;&raquo;,
&laquo;&nbsp;Num&eacute;ro d&rsquo;identit&eacute; nationale&nbsp;&raquo;
et &laquo;&nbsp;Derni&egrave;re date&nbsp;&raquo;)</I></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="18.3.1">18.3.1
	Date de visite ult&eacute;rieure &agrave; la date d&rsquo;entr&eacute;e des donn&eacute;es</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Les
fiches qui comportent une date de visite ult&eacute;rieure &agrave;
la date de saisie des donn&eacute;es. Ce type d&rsquo;erreur provoque
l&rsquo;affichage de donn&eacute;es erron&eacute;es dans les rapports
qui utilisent la date de visite pour dater les faits. Les r&eacute;sultats
de ce rapport affichent la date de visite entr&eacute;e sur la fiche,
celle entr&eacute;e dans le syst&egrave;me ainsi que le type de
fiches sur lesquelles il est possible de cliquer afin d&rsquo;afficher
et de corriger la fiche d&rsquo;origine. Ces donn&eacute;es
proviennent du champ intitul&eacute; &laquo;&nbsp;Date de visite&nbsp;&raquo;
situ&eacute; au sommet de toutes les fiches.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="18.3.2">18.3.2
	Mauvaise date de visite</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Les
fiches qui comportent une date de visite non valide. Ce type d&rsquo;erreur
provoque l&rsquo;affichage de donn&eacute;es erron&eacute;es dans les
rapports qui utilisent la date de visite pour dater les faits. Les
r&eacute;sultats de ce rapport affichent une mauvaise date de visite
entr&eacute;e sur la fiche ainsi que le type de fiches sur lesquelles
il est possible de cliquer afin d&rsquo;afficher et de corriger la
fiche d&rsquo;origine. Ces donn&eacute;es proviennent du champ
intitul&eacute; &laquo;&nbsp;Date de visite&nbsp;&raquo; situ&eacute;
au sommet de toutes les fiches.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="18.3.3">18.3.3
