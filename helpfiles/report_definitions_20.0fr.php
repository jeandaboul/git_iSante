<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=6 STYLE="font-size: 22pt"><B>Rapport de d&eacute;finition EMR</B></FONT></FONT></FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="20.0">20.0
	Qualit&eacute; des donn&eacute;es&nbsp;:&nbsp;Processus de gestion des donn&eacute;es</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
	Ces
rapports englobent tous les patients, quelque soit leur statut
d&rsquo;activit&eacute; et de traitement et sont destin&eacute;s &agrave;
produire des r&eacute;sultats individuels dans les diverses
cat&eacute;gories et a pour but de faciliter la gestion de la saisie
des donn&eacute;es.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="20.1">20.1
	Utilisation</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Afin
d&rsquo;utiliser ces rapports, veuillez simplement cliquer sur le
lien du titre de rapport. La page de param&egrave;tres de rapport ne
s&rsquo;applique pas &agrave; ces rapports.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="20.2">20.2
	R&eacute;sultats</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Les
r&eacute;sultats du rapport apparaissent dans une nouvelle fen&ecirc;tre
de navigateur ou un nouvel onglet, suivant la mani&egrave;re dont le
navigateur est configur&eacute;. Un exemple de l&rsquo;en-t&ecirc;te
des r&eacute;sultats est illustr&eacute; par la figure 22 dans la
section
	<A HREF="#20.2.1">20.2.1</A> </FONT><FONT FACE="Arial, sans-serif"><I>(ci-dessous)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="20.2.1"><IMG SRC="images/report_definitions_html_fr_m2291c657.jpg" NAME="graphics22" ALIGN=BOTTOM WIDTH=800 HEIGHT=78 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>20.2.1
	Figure 22 : En-t&ecirc;te de r&eacute;sultats pour le rapport &laquo;&nbsp;Patients avec uniquement une fiche d'enregistrement&nbsp;&raquo;</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Il
existe 2 boutons de commande au dessus du rapport de r&eacute;sultats.
Le bouton &laquo;&nbsp;Fermer&nbsp;&raquo;
ferme la fen&ecirc;tre de r&eacute;sultats (onglet). Le bouton
&laquo;&nbsp;excel&nbsp;&raquo; g&eacute;n&egrave;re une version
alternative du m&ecirc;me rapport de r&eacute;sultats sous le format
CSV.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Le
r&eacute;capitulatif des param&egrave;tres, se trouve sous les deux
boutons de commande. Chacune des colonnes peut &ecirc;tre tri&eacute;e
par ordre croissant ou d&eacute;croissant, en cliquant sur la fl&egrave;che
descendante (&agrave; gauche) ou la fl&egrave;che ascendante (&agrave;
droite), au niveau du titre de colonne.
	</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="20.3">20.3
	D&eacute;finitions importantes</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_6.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#6.3">6.3 &laquo;&nbsp;D&eacute;finitions importantes&nbsp;&raquo;</A> 
	ci-dessus pour les d&eacute;finitions des colonnes intitul&eacute;es
&laquo;&nbsp;No. de patient attribu&eacute; par le site&nbsp;&raquo;,
&laquo;&nbsp;Num&eacute;ro d&rsquo;identit&eacute; nationale&nbsp;&raquo;,
&laquo;&nbsp;Pr&eacute;nom&nbsp;&raquo;, &laquo;&nbsp;Nom&nbsp;&raquo;
et &laquo;&nbsp;Derni&egrave;re date&nbsp;&raquo;).</I></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="20.3.1">20.3.1
	Date de visite/Laps de temps avant la saisie des donn&eacute;es</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Ce
rapport affiche le d&eacute;lai, en nombre moyen de jour entre la
date de visite et la date de saisie des donn&eacute;es, sur la base
des donn&eacute;es entr&eacute;es durant la p&eacute;riode du
rapport. Ce rapport peut &ecirc;tre utilis&eacute; sur une base
mensuelle afin de suivre le processus de saisie de
donn&eacute;es&nbsp;.&nbsp;L&rsquo;objectif
souhaitable est une moyenne de moins de 3 jours et l&rsquo;objectif
acceptable est une moyenne de 3 jours. Ces donn&eacute;es proviennent
du champ intitul&eacute; &laquo;&nbsp;Date de visite&nbsp;&raquo;
situ&eacute; au sommet de toutes les fiches.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="20.3.2">20.3.2
	Fiches r&eacute;cemment saisies</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Ce
rapport liste les fiches qui ont r&eacute;cemment &eacute;t&eacute;
saisies dans le syst&egrave;me. Il invite l&rsquo;utilisateur &agrave;
entrer 2 param&egrave;tres additionnels avant d&rsquo;afficher les
r&eacute;sultats
	<I>(Voir
	<A HREF="#20.3.2.1">20.3.2.1 Figure 23</A> ci-dessous)</I>.
        Il requiert le
nombre maximum de fiches qui doivent &ecirc;tre list&eacute;es (la
valeur par d&eacute;faut affich&eacute;e est &laquo;&nbsp;100&nbsp;&raquo;)
et le nom d&rsquo;utilisateur de l&rsquo;op&eacute;rateur de saisie
des donn&eacute;es, lorsque uniquement les fiches entr&eacute;es par
cet utilisateur doivent &ecirc;tre affich&eacute;es dans le rapport
de r&eacute;sultats (entrer &laquo;&nbsp;%&nbsp;&raquo; pour ce
param&egrave;tre si tous les utilisateurs doivent &ecirc;tre
affich&eacute;s). Le rapport de r&eacute;sultats affiche le no. de
patient attribu&eacute; par l&rsquo;&eacute;tablissement, le nom
d&rsquo;utilisateur de l&rsquo;op&eacute;rateur qui a saisi la fiche,
la date de cr&eacute;ation, la date de modification la plus r&eacute;cente
de la fiche et le type de fiches sur lesquelles il est possible de
cliquer afin d&rsquo;afficher et de corriger la fiche d&rsquo;origine.
	</SPAN></FONT></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in; font-style: normal">
	<BR>
	</P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><IMG SRC="images/report_definitions_html_fr_m7d5ff893.jpg" NAME="graphics23" ALIGN=BOTTOM WIDTH=342 HEIGHT=121 BORDER=0>
	 
