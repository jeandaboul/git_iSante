	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>20.3.2.1
	Figure 23 : Param&egrave;tres additionnels sollicit&eacute;s
pour le rapport &laquo;&nbsp;Fiches r&eacute;cemment saisies&nbsp;&raquo;</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="20.3.3">20.3.3
	Nombre de fiches saisies la semaine derni&egrave;re</A></I></FONT><I>&nbsp;:&nbsp;</I> 
        Ce
rapport affiche le nombre de fiches entr&eacute;es dans le syst&egrave;me
lors des 7 derniers jours.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="20.3.4">20.3.4
	Nombre de fiches saisies le mois dernier</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Ce
rapport affiche le nombre de fiches entr&eacute;es dans le syst&egrave;me
lors des 30 derniers jours.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="20.3.5">20.3.5
	Patients avec uniquement une fiche d'enregistrement</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Ce
rapport recense les patients qui poss&egrave;dent une fiche
d&rsquo;enregistrement mais pas d&rsquo;autres fiches. Puisqu&rsquo;il
faut prendre en compte le d&eacute;lai possible entre les visites des
patients et la date de saisie, ce rapport examine les fiches
d&rsquo;enregistrement cr&eacute;&eacute;es au moins 7 jours avant la
date du rapport. Ce rapport permet aux responsables de qualit&eacute;
de donn&eacute;es d&rsquo;identifier les cas o&ugrave; les membres de
l&rsquo;&eacute;quipe m&eacute;dicale peuvent avoir oubli&eacute;
d&rsquo;utiliser les fiches m&eacute;dicales standardis&eacute;es
lors des prestations de soins aux patients.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="20.3.6">20.3.6
	Patients sans fiche de premi&egrave;re visite</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Ce
rapport recense les patients qui poss&egrave;dent une fiche
d&rsquo;enregistrement et au moins une fiche d&rsquo;un autre type
mais qui n&rsquo;ont pas de fiche de premi&egrave;re visite. Ce
rapport permet aux responsables de la qualit&eacute; de donn&eacute;es
d&rsquo;identifier les instances o&ugrave; les membres de l&rsquo;&eacute;quipe
m&eacute;dicale peuvent avoir oubli&eacute; d&rsquo;utiliser les
fiches m&eacute;dicales standardis&eacute;es lors des prestations de
soins aux patients.
	</SPAN></FONT></P>
