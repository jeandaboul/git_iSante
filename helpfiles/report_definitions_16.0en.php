	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="16.0">16.0
	Program Management: Service Delivery</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">These
	reports cover all patients regardless of activity and treatment
	status and are meant to give aggregated results in various service
	delivery categories for the whole clinic population.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="16.1">16.1
	Usage</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">To
	use these reports, simply click on the linked report title.  The
	report parameters page does not apply to these reports.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="16.2">16.2
	Results</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">The
	report results will appear in a new browser window (or a new browser
	tab, depending on how your browser is configured).  An example
	portion of this report is shown in <A HREF="#16.2.1">16.2.1 Figure 18</A> </FONT><FONT FACE="Arial, sans-serif"><I>(below)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="16.2.1"><IMG SRC="images/report_definitions_html_3ab4e638.jpg" NAME="graphics18" ALIGN=BOTTOM WIDTH=738 HEIGHT=137 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>16.2.1
	Figure 18: Part of the 'Visits by Month' report for H&ocirc;pital
	St. Michel</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">There
	are two action buttons at the top of the report results.  The
	'Close' button will close the report results window (or tab).  The
	'excel' button will generate an alternate version of the same report
	results in CSV format.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">Below
	the two action buttons is the date range used to generate the report
	results.  Underneath the date range is the results header.  Each of
	the columns can be sorted in ascending or descending order by
	clicking on the down arrow (left side) or up arrow (right side) next
	to the column title.</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="16.3">16.3
	Relevant Definitions</A></B></FONT></FONT><BR><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="16.3.1">16.3.1
	Visits by month</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The number of patient visits by month for the selected clinic site.
	Repeat visits during a single day (morning and afternoon) are
	counted as a single visit.  This </SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">data
	is derived from all of the completed patient forms entered at the
	specified clinic site.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="16.3.2">16.3.2
	New diagnoses made</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The distribution of diagnoses made at clinical intake or follow-up
	visits for the selected clinic site.  The counts do not represent
	unique patients.  If multiple diagnoses are indicated for a patient,
	then that patient would be counted in multiple categories.  If a
	patient receives the same diagnosis repeatedly, the diagnosis is
	counted each time it is made.  These data are derived from the
	intake and follow-up forms for all patients at the specified clinic
	site from the </SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">sections
	titled 'Past and Present Diagnoses by WHO Stage' or 'Current
	Diagnoses/Evaluation'.  </SPAN></SPAN></SPAN></SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><I><B><SPAN STYLE="background: transparent">Note:</SPAN></B></I></SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">
	 This section is titled 'Past and Present Diagnoses' on the
	pediatric versions of the forms.</SPAN></SPAN></SPAN></SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	</SPAN></FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="16.3.3">16.3.3
	Active diagnoses</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The distribution of active diagnoses made at clinical intake or
	follow-up visits for the selected clinic site.  The counts do not
	represent unique patients.  If multiple active diagnoses are
	indicated for a patient, then that patient would be counted in
	multiple categories.  These data are derived from all non-resolved
	diagnoses on the intake and follow-up forms for all patients at the
	specified clinic site in the </SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">sections
	titled 'Past and Present Diagnoses by WHO Stage' or 'Current
	Diagnoses/Evaluation'.  </SPAN></SPAN></SPAN></SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><I><B><SPAN STYLE="background: transparent">Note:</SPAN></B></I></SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">
	 This section is titled 'Past and Present Diagnoses' on the
	pediatric versions of the forms.</SPAN></SPAN></SPAN></SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	</SPAN></FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="16.3.4">16.3.4
	Laboratory tests ordered</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The distribution of lab tests ordered at the selected clinic site. 
	The counts do not represent unique patients.  If multiple lab tests
	were ordered for a patient, then that patient would be counted in
	multiple categories.  These data are derived from the laboratory
	forms for all patients at the specified clinic site in the </SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">section
	titled 'Labs Ordered and Results' under the heading titled 'Labs
	Ordered'</SPAN></SPAN></SPAN></SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">.</SPAN></SPAN></SPAN></SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	</SPAN></FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="16.3.5">16.3.5
	Laboratory tests completed</A></I></FONT><I>:</I>  The distribution of
	lab tests completed at the selected clinic site.  The counts do not
	represent unique patients.  If multiple lab tests were completed for
	a patient, then that patient would be counted in multiple
	categories.  These data are derived from all non-empty result fields
	on the laboratory forms for all patients at the specified clinic
	site in the <SPAN LANG="en-US"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">section
	titled 'Labs Ordered and Results' under the heading titled 'Result'</SPAN></SPAN></SPAN><SPAN LANG="en-US"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">.</SPAN></SPAN></SPAN>
	</FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="16.3.6">16.3.6
	Medications ordered</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The distribution of medications ordered at the selected clinic
	site.  The counts do not represent unique patients.  If multiple
	medications were ordered for a patient, then that patient would be
	counted in multiple categories.  If the same medication is ordered
	repeatedly for a patient, the medication is counted each time it is
	ordered.  These data are derived from the prescription forms for all
	patients at the specified clinic site in the </SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">section
	titled 'Prescription' under the various drug headings</SPAN></SPAN></SPAN></SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN LANG="en-US"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">.</SPAN></SPAN></SPAN></SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	</SPAN></FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="16.3.7">16.3.7
	Medications dispensed</A></I></FONT><I>:</I>  The distribution of
	medications dispensed at the selected clinic site.  The counts do
	not represent unique patients.  If multiple medications were
	dispensed for a patient, then that patient would be counted in
	multiple categories.  If the same medication is dispensed repeatedly
	for a patient, the medication is counted each time it is dispensed. 
	These data are derived from all drugs indicated as 'Dispensed' on
	the prescription forms for all patients at the specified clinic site
	in the <SPAN LANG="en-US"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">section
	titled 'Prescription' under the various drug </SPAN></SPAN></SPAN><SPAN LANG="en-US"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">headings</SPAN></SPAN></SPAN><SPAN LANG="en-US"><SPAN STYLE="font-weight: medium"><SPAN STYLE="background: transparent">.</SPAN></SPAN></SPAN>
	</FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
