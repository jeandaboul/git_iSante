	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="7.0">7.0
	Quality of Care: Appointment Reminders</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">There
	are two reports in this category that will provide a list of
	patients at the currently selected clinic site that are scheduled
	for follow-up visits in the next 7 and 14 days.  These reports may
	be used to plan follow-up care steps for specific patients
	(prospective care report use).  For example, the list could be used
	to pull the paper charts for all the patients that are due to be
	returning for a follow-up visit.  These reports do not display
	patients who are overdue for their next scheduled clinic visit.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="7.1">7.1
	Usage</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">To
	use either of these reports, simply click on the linked report
	title.  The report parameters page does not apply to these reports.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="7.2">7.2
	Results</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">The
	report results will appear in a new browser window (or a new browser
	tab, depending on how your browser is configured).  An example
	results header is shown in <A HREF="#7.2.1">7.2.1 Figure 3</A> </FONT><FONT FACE="Arial, sans-serif"><I>(below)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="7.2.1"><IMG SRC="images/report_definitions_html_7ef6553f.jpg" NAME="graphics3" ALIGN=BOTTOM WIDTH=800 HEIGHT=81 BORDER=0></A></P>
