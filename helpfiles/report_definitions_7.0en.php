	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="7.3.2">7.3.2
	Next visit date</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The date(s) for any next scheduled visit(s) for the patient at the
	currently selected clinic site.  This data element is derived from
	the date(s) entered on the patient's intake, follow-up or
	prescription forms in the field titled 'Next Visit Date' or 'Next
	Prescription Pick-up Date'.</SPAN></FONT></P>
