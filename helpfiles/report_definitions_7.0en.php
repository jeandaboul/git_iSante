	Patient status</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif">
	 The most recent status for the patient during the time frame for
	the report </FONT><FONT FACE="Arial, sans-serif"><I>(see section <A HREF="helpfile.php?file=report_definitions_4.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#4.0">4.0
