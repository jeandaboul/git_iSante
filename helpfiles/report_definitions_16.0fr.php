<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=6 STYLE="font-size: 22pt"><B>Rapport de d&eacute;finition EMR</B></FONT></FONT></FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="16.0">16.0
	Gestion de programmes&nbsp;:&nbsp;Services fournis</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">
        Ces
rapports englobent tous les patients, quelque soit leur statut
d&rsquo;activit&eacute; ou de traitement et sont destin&eacute;s &agrave;
produire des r&eacute;sultats agr&eacute;g&eacute;s pour les services
fournis par un &eacute;tablissement m&eacute;dical.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="16.1">16.1
	Utilisation</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Afin
d&rsquo;utiliser ces rapports, veuillez simplement cliquer sur le
lien du titre de rapport. La page de param&egrave;tres de rapport ne
s&rsquo;applique pas &agrave; ces rapports.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="16.2">16.2
	R&eacute;sultats</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Les
r&eacute;sultats du rapport apparaissent dans une nouvelle fen&ecirc;tre
de navigateur ou un nouvel onglet, suivant la mani&egrave;re dont le
navigateur est configur&eacute;. Un exemple d&rsquo;une partie de ce
rapport est illustr&eacute; par la figure 18 dans la section
	<A HREF="#16.2.1">16.2.1</A> </FONT><FONT FACE="Arial, sans-serif"><I>(below)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="16.2.1"><IMG SRC="images/report_definitions_html_fr_3ab4e638.jpg" NAME="graphics18" ALIGN=BOTTOM WIDTH=726 HEIGHT=135 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>16.2.1
	Figure 18 : Extrait
du rapport &laquo;&nbsp;Nombre de visites par mois&nbsp;&raquo; pour l&rsquo;H&ocirc;pital St. Michel</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Il existe
2 boutons de commande au dessus du rapport de r&eacute;sultats. Le
bouton &laquo;&nbsp;Fermer&nbsp;&raquo; ferme la fen&ecirc;tre de
r&eacute;sultats (onglet). Le bouton &laquo;&nbsp;excel&nbsp;&raquo;
g&eacute;n&egrave;re une version alternative du m&ecirc;me rapport de
r&eacute;sultats sous le format CSV.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Le
r&eacute;capitulatif des param&egrave;tres, se trouve sous les deux
boutons de commande. Chacune des colonnes peut &ecirc;tre tri&eacute;e
par ordre croissant ou d&eacute;croissant, en cliquant sur la fl&egrave;che
descendante (&agrave; gauche) ou la fl&egrave;che ascendante (&agrave;
droite), au niveau du titre de colonne.
	</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="16.3">16.3
	D&eacute;finitions importantes</A></B></FONT></FONT><BR><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="16.3.1">16.3.1
	Nombre de visites par mois</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le nombre de
visites de patients par mois pour l&rsquo;&eacute;tablissement
m&eacute;dical s&eacute;lectionn&eacute;. Les visites r&eacute;p&eacute;titives
effectu&eacute;es le m&ecirc;me jour (matin et apr&egrave;s-midi)
sont consid&eacute;r&eacute;es comme une seule visite. Ces donn&eacute;es
proviennent de toutes les fiches compl&eacute;t&eacute;es au sein de
l&rsquo;&eacute;tablissement m&eacute;dical sp&eacute;cifi&eacute;.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="16.3.2">16.3.2
	Diagnostics nouvellement &eacute;tablis</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le
nombre de diagnostics &eacute;tablis lors des premi&egrave;res
visites ou des visites de suivi de patients au sein de
l&rsquo;&eacute;tablissement m&eacute;dical s&eacute;lectionn&eacute;.
Le nombre ne repr&eacute;sente pas forc&eacute;ment un nombre de
patients. Si des diagnostics multiples sont indiqu&eacute;s pour un
patient, ce patient sera alors comptabilis&eacute; dans plusieurs
cat&eacute;gories. Si un patient re&ccedil;oit le m&ecirc;me
diagnostic plusieurs fois, le diagnostic sera comptabilis&eacute; &agrave;
chaque fois. Ces donn&eacute;es proviennent des fiches de premi&egrave;re
visite et de suivi compl&eacute;t&eacute;es au sein de
l&rsquo;&eacute;tablissement m&eacute;dical sp&eacute;cifi&eacute;,
dans la section intitul&eacute;e &laquo;&nbsp;Ant&eacute;c&eacute;dents
m&eacute;dicaux et diagnostics actuels&nbsp;&raquo; ou
&laquo;&nbsp;Diagnostic actuel/&eacute;volution&nbsp;&raquo;.
<I><B>Note&nbsp;:&nbsp;</B></I>Cette
section est intitul&eacute;e &laquo;&nbsp;Ant&eacute;c&eacute;dents
m&eacute;dicaux et diagnostics actuels&nbsp;&raquo;
sur la version p&eacute;diatrique des fiches.
	</SPAN></SPAN></SPAN></SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	</SPAN></FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="16.3.3">16.3.3
	Diagnostics actifs</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le
nombre de diagnostics actifs &eacute;tablis lors des premi&egrave;res
visites ou des visites de suivi de patients au sein de
l&rsquo;&eacute;tablissement m&eacute;dical s&eacute;lectionn&eacute;.
Le nombre ne repr&eacute;sente pas forc&eacute;ment un nombre de
patients. Si des diagnostics actifs multiples sont indiqu&eacute;s
pour un patient, ce patient sera alors comptabilis&eacute; dans
plusieurs cat&eacute;gories. Ces donn&eacute;es proviennent des
diagnostics non r&eacute;solus sur fiches de premi&egrave;re visite
et de suivi compl&eacute;t&eacute;es au sein de l&rsquo;&eacute;tablissement
m&eacute;dical sp&eacute;cifi&eacute;, dans la section intitul&eacute;e
 &laquo;&nbsp;Ant&eacute;c&eacute;dents m&eacute;dicaux et
diagnostics actuels&nbsp;&raquo; ou &laquo;&nbsp;Diagnostic
actuel/&eacute;volution&nbsp;&raquo;. <I><B>Note&nbsp;:&nbsp;</B></I>Cette
section est intitul&eacute;e &laquo;&nbsp;Ant&eacute;c&eacute;dents
m&eacute;dicaux et diagnostics actuels&nbsp;&raquo;
sur la version p&eacute;diatrique des fiches.
	</SPAN></SPAN></SPAN></SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	</SPAN></FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="16.3.4">16.3.4
	Analyses de laboratoire prescrites</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le
nombre d&rsquo;analyses de laboratoires prescrites au sein de
l&rsquo;&eacute;tablissement m&eacute;dical s&eacute;lectionn&eacute;.
Le nombre ne repr&eacute;sente pas forc&eacute;ment un nombre de
patients. Si des analyses de laboratoires multiples sont prescrites
pour un patient, ce patient sera alors comptabilis&eacute; dans
plusieurs cat&eacute;gories. Ces donn&eacute;es proviennent des
fiches de laboratoires compl&eacute;t&eacute;es au sein de
l&rsquo;&eacute;tablissement m&eacute;dical sp&eacute;cifi&eacute;,
dans la section intitul&eacute;e &laquo;&nbsp;Analyses demand&eacute;es
et r&eacute;sultats&nbsp;&raquo; sous le titre &laquo;&nbsp;Analyses
demand&eacute;es&nbsp;&raquo;.
	</SPAN></FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="16.3.5">16.3.5
	Analyses de laboratoire effectu&eacute;s</A></I></FONT><I>&nbsp;:&nbsp;</I> 
        Le
nombre d&rsquo;analyses de laboratoires effectu&eacute;es au sein de
l&rsquo;&eacute;tablissement m&eacute;dical s&eacute;lectionn&eacute;.
Le nombre ne repr&eacute;sente pas forc&eacute;ment un nombre de
patients. Si des d&rsquo;analyses de laboratoires multiples sont
effectu&eacute;es pour un patient, ce patient sera alors comptabilis&eacute;
dans plusieurs cat&eacute;gories. Ces donn&eacute;es proviennent des
champs de r&eacute;sultats non vides situ&eacute;s sur les fiches de
laboratoires compl&eacute;t&eacute;es au sein de l&rsquo;&eacute;tablissement
m&eacute;dical sp&eacute;cifi&eacute;, dans la section intitul&eacute;e
&laquo;&nbsp;Analyses demand&eacute;es et r&eacute;sultats&nbsp;&raquo;
sous le titre &laquo;&nbsp;R&eacute;sultats&nbsp;&raquo;.
	</FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="16.3.6">16.3.6
	M&eacute;dicaments prescrits</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le nombre
de m&eacute;dicaments prescrits au sein de l&rsquo;&eacute;tablissement
m&eacute;dical s&eacute;lectionn&eacute;. Le nombre ne repr&eacute;sente
pas forc&eacute;ment un nombre de patients. Si plusieurs m&eacute;dicaments
sont prescrits pour un patient, ce patient sera alors comptabilis&eacute;
dans plusieurs cat&eacute;gories. Si le m&ecirc;me m&eacute;dicament
est prescrit plusieurs fois pour un patient, le patient sera alors
comptabilis&eacute; &agrave; chaque fois qu&rsquo;il est prescrit.
Ces donn&eacute;es proviennent des fiches de pharmacie compl&eacute;t&eacute;es
au sein de l&rsquo;&eacute;tablissement m&eacute;dical sp&eacute;cifi&eacute;,
dans la section intitul&eacute;e &laquo;&nbsp;Ordonnance&nbsp;&raquo;
sous les diverses en-t&ecirc;tes des m&eacute;dicaments.
	</SPAN></FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="16.3.7">16.3.7
