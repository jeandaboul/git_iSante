<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=6 STYLE="font-size: 22pt"><B>EMR
Report Definitions</B></FONT></FONT></FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="15.0">15.0
	Program Management: Patient Demographics</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">These
	reports cover all patients regardless of activity and treatment
	status and are meant to give aggregated results in various
	demographic categories for the whole clinic population.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="15.1">15.1
	Usage</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">To
	use these reports, select a date range on the report parameters page
	to be used in generating the report results that are desired </FONT><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_5.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#5.0">5.0 Report Parameters Page</A> above for definitions of the
	sections on the report parameters page)</I></FONT><FONT FACE="Arial, sans-serif">.
	 Then, click the 'Submit' button.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><B>Note:</B></FONT><FONT FACE="Arial, sans-serif">
	Only the 'Start Date' and 'End Date' fields are selectable in these
	reports since the results are meant to cover all patients at the
	reporting clinic site.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="15.2">15.2
	Results</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">The
	report results will appear in a new browser window (or a new browser
	tab, depending on how your browser is configured).  An example
	patient demographics report is shown in <A HREF="#15.2.1">15.2.1 Figure 17</A> </FONT><FONT FACE="Arial, sans-serif"><I>(below)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="15.2.1"><IMG SRC="images/report_definitions_html_376745aa.jpg" NAME="graphics17" WIDTH=636 HEIGHT=296 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>15.2.1
	Figure 17: Sample 'Age at First Visit' report</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">There
	are two action buttons at the top of the report results.  The
	'Close' button will close the report results window (or tab).  The
	'excel' button will generate an alternate version of the same report
	results in CSV format.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">Below
	the two action buttons is a summary of the parameters used to
	generate the report results as provided on the report parameters
	page.  Underneath the parameter summary is the results header.  Each
	of the columns can be sorted in ascending or descending order by
	clicking on the down arrow (left side) or up arrow (right side) next
	to the column title.</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="15.3">15.3
	Relevant Definitions</A></B></FONT></FONT><BR><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="15.3.1">15.3.1
	Patient count</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The total count of all non-discontinued patients regardless of
	activity status </SPAN></FONT><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_4.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#4.0">4.0 Classification of Patients by Activity Status</A> above for
	definitions of patient activity statuses)</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	for the specified date range.  This data is derived from all
	completed patient registration forms in the EMR for the specified
	clinic site.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="15.3.2">15.3.2
	Patients by age group</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 Total counts of all non-discontinued patients broken into age
	groups (0-17, 18-20, 21-30, 31-40, 41-50, 51-60, 61-70, 71-80 and
	unknown) for the specified date range.  This data is derived from
	the completed patient registration form at the specified clinic
	site.  The ages are computed from the fields titled 'Date of Birth'
	or 'If date unknown, age in years' on the patient's registration
	form.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="15.3.3">15.3.3
	Age at first visit</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 Total counts of all non-discontinued patients broken into age
	groups (0-17, 18-20, 21-30, 31-40, 41-50, 51-60, 61-70, 71-80 and
	unknown) at their first visit.  This data is derived from the
	completed patient registration form at the specified clinic site. 
	The ages are computed from the fields titled 'Date of Birth' or 'If
	date unknown, age in years' on the patient's registration form.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="15.3.4">15.3.4
	Count by gender</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 Total counts of all non-discontinued patients broken into gender
	groups (male and female).  This data is derived from the completed
	patient registration form at the specified clinic site.  The gender
	is taken from the field titled 'Sex' on the patient's registration
	form.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="15.3.5">15.3.5
	Risk factors for HIV transmission</A></I></FONT><I>:</I>  Total counts
	of the various HIV risk factors for all non-discontinued patients. 
	This data is derived from the patient intake forms at the specified
	clinic site from the section titled 'Probable Mode of
	Transmission/Risk Assessment'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
