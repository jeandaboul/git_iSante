<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=6 STYLE="font-size: 22pt"><B>Rapport de d&eacute;finition EMR</B></FONT></FONT></FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="8.0">8.0
	Qualit&eacute; des soins&nbsp;:&nbsp;Rappels d'analyses de laboratoire</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Trois
rapports de cette cat&eacute;gorie affichent une liste de patients de
l&rsquo;&eacute;tablissement clinique actuellement s&eacute;lectionn&eacute;
dont un test de laboratoire particulier a &eacute;t&eacute; effectu&eacute;,
n&rsquo;a pas &eacute;t&eacute; effectu&eacute; ou doit &ecirc;tre
effectu&eacute; dans les 30 jours suivants. Ces rapports peuvent &ecirc;tre
utilis&eacute;s pour identifier si un patient a effectu&eacute; un
test sp&eacute;cifique et planifier les &eacute;tapes de suivi de
soins (utilisation &eacute;ventuelle de rapport de soins). Par
exemple, un rapport de &laquo;&nbsp;Test effectu&eacute;&nbsp;&raquo;
permet de d&eacute;terminer rapidement la liste des patients qui ont
d&eacute;j&agrave; effectu&eacute; un test donn&eacute; dans
l&rsquo;&eacute;tablissement clinique qui effectue le rapport.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="8.1">8.1
	Utilisation</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Afin
d&rsquo;utiliser ce rapport, veuillez simplement s&eacute;lectionner
un ou plusieurs type(s) de statut de patient dans la section du
rapport &laquo;&nbsp;page de param&egrave;tres&nbsp;&raquo;,
puis s&eacute;lectionner une p&eacute;riode de temps, et tout autre
crit&egrave;re &agrave; utiliser, sur cette page, afin de g&eacute;n&eacute;rer
les r&eacute;sultats de rapport souhait&eacute;s
	</FONT><FONT FACE="Arial, sans-serif"><I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_5.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#5.0">5.0 &laquo;&nbsp;Page de param&egrave;tres de rapport&nbsp;&raquo;</A>,
        ci-dessus pour les d&eacute;finitions des sections de la page de param&egrave;tres du rapport)</I></FONT><FONT FACE="Arial, sans-serif">.
        Cliquez ensuite sur le bouton &laquo;&nbsp;Soumettre&nbsp;&raquo;.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><B>Note&nbsp;:&nbsp;</B></FONT><FONT FACE="Arial, sans-serif">
        Le
rapport &laquo;&nbsp;Test n&eacute;cessaire sous 30 jours&nbsp;&raquo;
limite la liste de tests &agrave; s&eacute;lectionner au CD4, bilan
h&eacute;patique et h&eacute;mogramme.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="8.2">8.2
	R&eacute;sultats</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Les
r&eacute;sultats du rapport apparaissent dans une nouvelle fen&ecirc;tre
de navigateur ou un nouvel onglet, suivant la mani&egrave;re dont le
navigateur est configur&eacute;. Un exemple de l&rsquo;en-t&ecirc;te
des r&eacute;sultats est illustr&eacute; par la figure 4 de la
section
	<A HREF="#8.2.1">8.2.1</A> </FONT><FONT FACE="Arial, sans-serif"><I>(ci-dessous)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="8.2.1"><IMG SRC="images/report_definitions_html_fr_m2f93c839.jpg" NAME="graphics4" ALIGN=BOTTOM WIDTH=800 HEIGHT=134 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>8.2.1
	Figure 4 : En-t&ecirc;te de
r&eacute;sultats pour le rapport de &laquo;&nbsp;Test effectu&eacute;&nbsp;&raquo;</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Il
existe 3 boutons de commande au sommet des r&eacute;sultats du
rapport. Le bouton &laquo;&nbsp;Fermer&nbsp;&raquo;
ferme la fen&ecirc;tre de r&eacute;sultats (onglet). Les boutons
&laquo;&nbsp;pdf&nbsp;&raquo; et &laquo;&nbsp;excel&nbsp;&raquo;
g&eacute;n&egrave;rent des versions alternatives du m&ecirc;me
rapport de r&eacute;sultats, soit respectivement sous PDF ou sous
CSV.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        La
p&eacute;riode de temps &agrave; utiliser pour produire le rapport se
situe au dessous des 3 boutons de commande. L&rsquo;en-t&ecirc;te de
r&eacute;sultats se situe sous la p&eacute;riode de temps. Chacune
des colonnes peut &ecirc;tre tri&eacute;e par ordre croissant ou
d&eacute;croissant, en cliquant sur la fl&egrave;che descendante (&agrave;
gauche) ou la fl&egrave;che ascendante (&agrave; droite), au niveau du
titre de colonne.
	</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="8.3">8.3
	D&eacute;finitions importantes</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_6.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#6.3">6.3 &laquo;&nbsp;D&eacute;finitions importantes&nbsp;&raquo;</A> 
        ci-dessus pour les d&eacute;finitions des colonnes intitul&eacute;es
&laquo;&nbsp;No. de patient attribu&eacute; par le site&nbsp;&raquo;,
&laquo;&nbsp;Num&eacute;ro d&rsquo;identit&eacute; nationale&nbsp;&raquo;,
&laquo;&nbsp;Pr&eacute;nom&nbsp;&raquo;,
&laquo;&nbsp;Nom&nbsp;&raquo;,
&laquo;&nbsp;Sexe&nbsp;&raquo;, &laquo;&nbsp;Age&nbsp;&raquo;,
&laquo;&nbsp;Statut&nbsp;&raquo;
et &laquo;&nbsp;Derni&egrave;re date&nbsp;&raquo;).
	</I></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><FONT SIZE=3><I><A NAME="8.3.1">8.3.1
	Test jamais effectu&eacute;</A></I></FONT></FONT><I>&nbsp;:&nbsp;</I>
        Un patient qui n&rsquo;a jamais effectu&eacute; le test indiqu&eacute;.
Cette donn&eacute;e provient des r&eacute;sultats d&rsquo;analyses de
laboratoire effectu&eacute;es et saisies sur la fiche de laboratoire
du patient dans la colonne &laquo;&nbsp;R&eacute;sultats&nbsp;&raquo;.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><FONT SIZE=3><I><A NAME="8.3.2">8.3.2
	Test effectu&eacute;</A></I></FONT></FONT><I>&nbsp;:&nbsp;</I>
        Un patient qui a effectu&eacute; le test indiqu&eacute;. Cette donn&eacute;e
provient des r&eacute;sultats d&rsquo;analyses de laboratoire
effectu&eacute;es et saisies sur la fiche de laboratoire du patient
dans la colonne &laquo;&nbsp;R&eacute;sultats &raquo;.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><FONT SIZE=3><I><A NAME="8.3.3">8.3.3
	Test n&eacute;cessaire sous 30 jours</A></I></FONT></FONT><I>&nbsp;:&nbsp;</I>
        Un patient qui devra effectuer un test suppl&eacute;mentaire sous 30
jours. Cette donn&eacute;e provient des r&eacute;sultats d&rsquo;analyses
de laboratoire effectu&eacute;es et saisies sur la fiche de
laboratoire du patient dans la colonne &laquo;&nbsp;Date du r&eacute;sultats
&raquo;. Pour tous les types de patients, &laquo;&nbsp;Test
n&eacute;cessaire&nbsp;&raquo; signifie que
le test n&rsquo;a pas &eacute;t&eacute; effectu&eacute; dans les 8
derniers mois.
	</FONT></P>
