	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Deux
rapports de cette cat&eacute;gorie affichent une liste de patients de
l&rsquo;&eacute;tablissement m&eacute;dical actuellement s&eacute;lectionn&eacute;,
dont des visites de suivi sont planifi&eacute;es dans les 7 &agrave;
14 jours suivants. Ces rapports peuvent &ecirc;tre utilis&eacute;s
pour planifier les &eacute;tapes de suivi de patients sp&eacute;cifiques
(utilisation &eacute;ventuelle de rapport de soins). Par exemple, la
liste peut &ecirc;tre utilis&eacute;e pour extraire les dossiers
papiers de tous les patients qui doivent revenir pour une visite de
suivi. Ces rapports n&rsquo;affichent pas les patients qui ont
d&eacute;pass&eacute; la date limite de leur prochaine visite.
