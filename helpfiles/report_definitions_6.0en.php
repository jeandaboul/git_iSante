<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=6 STYLE="font-size: 22pt"><B>EMR
Report Definitions</B></FONT></FONT></FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="6.0">6.0
	Active and Inactive Patients report</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">This
	report may be used for a variety of purposes.  It may be used to
	plan follow-up care steps for specific patients (prospective care
	report use).  For example, a report showing 'at risk of
	discontinuation' patients may be used by clinic team members to
	identify patients requiring a home visit.  Other patient status
	reports may be used for retrospective analysis of quality of care
	issues.  For example, a report showing discontinued patients can be
	used to look for patterns in age and gender among discontinued
	patients, or for patterns in reasons for discontinuation, to guide
	analysis of ways to improve care services.  It may also be used to
	simply get a count of patients that have been entered into the EMR
	at the reporting clinic site.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="6.1">6.1
	Usage</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">To
	use this report, simply select one or more patient status groups
	from the 'Patient Status' section of the report parameters page,
	select a date range and select any other criteria on that page to be
	used in generating the report results that are desired </FONT><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_5.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>&">5.0 Report Parameters Page</A> above for definitions of the
	sections on the report parameters page)</I></FONT><FONT FACE="Arial, sans-serif">.
	 Then, click the 'Submit' button.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="6.2">6.2
	Results</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">The
	report results will appear in a new browser window (or a new browser
	tab, depending on how your browser is configured).  The results
	header for this report is shown in <A HREF="#6.2.1">6.2.1 Figure 2</A> </FONT><FONT FACE="Arial, sans-serif"><I>(below)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="6.2.1"><IMG SRC="images/report_definitions_html_69b388c9.jpg" NAME="graphics2" ALIGN=BOTTOM WIDTH=800 HEIGHT=136 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>6.2.1
	Figure 2: Results header for the 'Active/Inactive Patients' report</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">There
	are three action buttons at the top of the report results.  The
	'Close' button will close the report results window (or tab).  The
	'pdf' and 'excel' buttons will generate alternate versions of the
	same report results in either PDF or CSV formats respectively.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">Below
	the three action buttons are the date range and a summary of the
	parameters used to generate the report results as provided on the
	report parameters page.  Underneath the parameter summary is the
	results header.  Each of the columns can be sorted in ascending or
	descending order by clicking on the down arrow (left side) or up
	arrow (right side) next to the column title.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="6.3">6.3
	Relevant Definitions</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.1">6.3.1
	Clinic patient ID</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif">
	 The ID assigned to the patient at the clinic site.  This data
	element is derived from the text entered on the patient's
	registration form in the section titled 'Demographic Information' in
	the field titled 'Clinic ID'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.2">6.3.2
	National ID</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif">
	 The ID assigned to the patient based on an algorithm established at the national level.  This data
	element is derived from the text entered on the patient's
	registration form in the section titled 'Demographic Information' in
	the field titled 'National ID'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.3">6.3.3
	First name</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif">
	 The patient's given name.  This data element is derived from the
	text entered on the patient's registration form in the section
	titled 'Demographic Information' in the field titled 'First Name'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.4">6.3.4
	Last name</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif">
	 The patient's family name.  This data element is derived from the
	text entered on the patient's registration form in the section
	titled 'Demographic Information' in the field titled 'Last Name'.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.5">6.3.5
	Gender</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The patient's gender.  This data element is derived from the
	selected item entered on the patient's registration form in the
	section titled 'Demographic Information' for the field titled 'Sex'.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.6">6.3.6
	Age</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The age of the patient in years at the time their registration form
	was filled out.  This data element is derived from the date and the
	text entered on the patient's registration form in the section
	titled 'Demographic Information' for the fields titled 'Date of
	Birth' and 'If date unknown, age in years' respectively.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.7">6.3.7
	Status</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	The most recent treatment status of the patient during the specified time
	frame, either 'On ART' or 'Palliative Care' depending
	on whether the patient has ever received ART medications at the reporting clinic site.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.8">6.3.8
	Last date</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The most recent visit date for the patient during the specified
	time frame.  This data element is derived from all of the visit
	dates entered on the patient's forms that have been entered into the
	EMR.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
