<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=6 STYLE="font-size: 22pt"><B>EMR
Report Definitions</B></FONT></FONT></FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="12.0">12.0
	Quality of Care: Appropriate lab test indicators</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">These
	reports indicate the level of performance of quality of care indicators. For example, a retrospective report on CD4 testing could show the proportion of active patients on ART who had a CD4 count ordered and completed in the past 8 months.  Reports indicating performance levels below stated goals, or determined to be inadequate, may also indicate a  need for improved documentation or tracking mechanisms.  Reports indicating poor performance can be further evaluated by other mechanisms, for example, individual patient record review.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="12.1">12.1
	Usage</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">To
	use these reports, simply select one or more patient status groups
	from the 'Patient Status' section of the report parameters page,
	select a test from the 'Test Type' section and select any other
	criteria on that page to be used in generating the report results
	that are desired </FONT><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_5.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#5.0">5.0 Report Parameters Page</A> above for definitions of the
	sections on the report parameters page)</I></FONT><FONT FACE="Arial, sans-serif">.
	 Then, click the 'Submit' button.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><B>Note:</B></FONT><FONT FACE="Arial, sans-serif">
	The 'Start Date' and 'End Date' fields are not selectable in these
	reports since the results are meant to cover the entire history of
	data at the reporting clinic site.  Also, the 'Up-to-date on test'
	report restricts the list of selectable tests to CD4, liver function
	and CBC tests.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="12.2">12.2
	Results</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">The
	report results will appear in a new browser window (or a new browser
	tab, depending on how your browser is configured).  An example
	results header is shown in <A HREF="#12.2.1">12.2.1 Figure 13</A> </FONT><FONT FACE="Arial, sans-serif"><I>(below)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="12.2.1"><IMG SRC="images/report_definitions_html_m195a4a6b.jpg" NAME="graphics13" ALIGN=BOTTOM WIDTH=800 HEIGHT=382 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>12.2.1
	Figure 13: Results header for the 'Ever had test' report</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">There
	are three action buttons at the top of the report results.  The
	'Close' button will close the report results window (or tab).  The
	'pdf' and 'excel' buttons will generate alternate versions of the
	same report results in either PDF or CSV formats respectively.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">Below
	the three action buttons is a summary of the parameters used to
	generate the report results as provided on the report parameters
	page.  Underneath the parameter summary is a chart displaying the
	results by percentage over the entire time period broken into
	six-month intervals for the selected clinic site.  Beneath the chart
	is the results header.  Each of the columns can be sorted in
	ascending or descending order by clicking on the down arrow (left
	side) or up arrow (right side) next to the column title.</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="12.3">12.3
	Relevant Definitions</A></B></FONT></FONT><BR><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="12.3.1">12.3.1
	Time period</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The six-month time periods covering all available data at the
	selected clinic site.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="12.3.2">12.3.2
	Numerator</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 For the 'Ever had test' report, the number of patients at the
	selected clinic site who have had a result for the indicated test
	entered on a laboratory form at the clinic.  This data is derived
	from the result fields for the indicated test on all laboratory
	forms.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">For
	the 'Up-to-date on test' report, the number of patients who are
	up-to-date on the indicated test at the clinic.  For all patients,
        'up-to-date' is defined as having a test result within the last
	8 months.  This data is derived from the result fields for the
        indicated test on all laboratory forms.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="12.3.3">12.3.3
	Denominator</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The total number of patients at the selected clinic site who have had at least one visit during the
	indicated time frame.  This data is derived from all of the patient
	registration forms entered for the selected clinic site.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="12.3.4">12.3.4
	Percentage</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 This data is computed by dividing the numerator by the denominator.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
