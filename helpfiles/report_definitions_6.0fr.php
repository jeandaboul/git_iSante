<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=6 STYLE="font-size: 22pt"><B>Rapport de d&eacute;finition EMR</B></FONT></FONT></FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="6.0">6.0
	Rapport de patients actifs et inactifs</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Ce
rapport peut &ecirc;tre utilis&eacute; pour diverses raisons. Il peut
servir &agrave; planifier les &eacute;tapes de suivi de soins pour
des patients sp&eacute;cifiques (utilisation &eacute;ventuelle de
rapport de soins). Par exemple, un rapport montrant des patients &laquo;&nbsp;en
danger de discontinuation&nbsp;&raquo; peut
&ecirc;tre utilis&eacute; par des membres de l'&eacute;quipe clinique
pour identifier les patients ayant besoin d'une visite &agrave;
domicile. D'autres rapports sur l'&eacute;tat des patients peuvent
&ecirc;tre utilis&eacute;s pour une analyse r&eacute;trospective de
la qualit&eacute; des soins. Par exemple, un rapport montrant les
patients discontinu&eacute;s peut &ecirc;tre utilis&eacute; afin de
rechercher des tendances dans l'&acirc;ge et le sexe des patients
discontinu&eacute;s, ou pour des mod&egrave;les de raisons de
discontinuation et permettre une analyse d&rsquo;am&eacute;lioration
des services de soins. Il peut, &eacute;galement, simplement &ecirc;tre
utilis&eacute; afin d&rsquo;obtenir un nombre de patients qui ont &eacute;t&eacute;
saisis dans l'EMR au sein de l&rsquo;&eacute;tablissement qui
effectue le rapport.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="6.1">6.1
	Utilisation</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Afin
d&rsquo;utiliser ce rapport, veuillez simplement s&eacute;lectionner
un ou plusieurs type(s) de statut de patient dans la section du
rapport &laquo;&nbsp;page de param&egrave;tres&nbsp;&raquo;,
puis s&eacute;lectionner une p&eacute;riode de temps, et tout autre
crit&egrave;re &agrave; utiliser,sur cette page, afin de g&eacute;n&eacute;rer
les r&eacute;sultats de rapport souhait&eacute;s
	</FONT><FONT FACE="Arial, sans-serif"><I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_5.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>&">5.0 &laquo;&nbsp;Page de param&egrave;tres de rapport&nbsp;&raquo;</A>, ci-dessus
        pour les d&eacute;finitions des sections de la page de
param&egrave;tres du rapport).
	</I></FONT><FONT FACE="Arial, sans-serif">.
	Cliquez ensuite sur le bouton &laquo;&nbsp;Soumettre&nbsp;&raquo;.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="6.2">6.2
	R&eacute;sultats</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Les
r&eacute;sultats du rapport apparaissent dans une nouvelle fen&ecirc;tre
de navigateur (ou un nouvel onglet, suivant la mani&egrave;re dont le
navigateur est configur&eacute;. Un exemple de l&rsquo;en-t&ecirc;te
de r&eacute;sultats pour ce rapport est illustr&eacute; par la figure
2, dans la section
	 <A HREF="#6.2.1">6.2.1</A> </FONT><FONT FACE="Arial, sans-serif"><I>(ci-dessous)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="6.2.1"><IMG SRC="images/report_definitions_html_fr_69b388c9.jpg" NAME="graphics2" ALIGN=BOTTOM WIDTH=800 HEIGHT=121 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>6.2.1
	Figure 2 : En-t&ecirc;te de r&eacute;sultats pour le rapport &laquo;&nbsp;Patients Actifs/Inactifs&nbsp;&raquo;</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Il
existe 3 boutons de commande au sommet des r&eacute;sultats du
rapport. Le bouton &laquo;&nbsp;Fermer&nbsp;&raquo;
ferme la fen&ecirc;tre de r&eacute;sultats (onglet). Les boutons
&laquo;&nbsp;pdf&nbsp;&raquo; et &laquo;&nbsp;excel&nbsp;&raquo;
g&eacute;n&egrave;rent des versions alternatives du m&ecirc;me
rapport de r&eacute;sultats, soit respectivement sous PDF ou sous
CSV.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        La
p&eacute;riode de temps et un r&eacute;sum&eacute; des param&egrave;tres
&agrave; utiliser pour produire un rapport se situent au dessous des
3 boutons de commande, de mani&egrave;re similaire &agrave; la page
de param&egrave;tres de rapport. L&rsquo;en-t&ecirc;te de r&eacute;sultats
se situe sous le r&eacute;sum&eacute; des param&egrave;tres. Chacune
des colonnes peut &ecirc;tre tri&eacute;e par ordre croissant ou
d&eacute;croissant, en cliquant sur la fl&egrave;che descendante (&agrave;
gauche) ou la fl&egrave;che ascendante (&agrave; droite), au niveau
du titre de colonne.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="6.3">6.3
	D&eacute;finitions importantes</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.1">6.3.1
	No. de patient attribu&eacute; par le site</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif">
        Le num&eacute;ro d&rsquo;identit&eacute; attribu&eacute; &agrave; un
patient au sein d&rsquo;un &eacute;tablissement donn&eacute;. Les
&eacute;l&eacute;ments de ces donn&eacute;es proviennent du texte
saisi sur la fiche d&rsquo;enregistrement du patient dans la section
&laquo;&nbsp;Informations d&eacute;mographiques&nbsp;&raquo;
dans le champ &laquo;&nbsp;No d&rsquo;&eacute;tablissement &raquo;.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.2">6.3.2
	Num&eacute;ro d&rsquo;identit&eacute; nationale</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif">
        Le
num&eacute;ro d&rsquo;identit&eacute; attribu&eacute; au patient sur
la base d&rsquo;un algorithme &agrave; l&rsquo;&eacute;chelle
nationale. Ces &eacute;l&eacute;ments de donn&eacute;es proviennent
du texte saisi sur la fiche d&rsquo;enregistrement du patient, dans
la section &laquo;&nbsp;Informations d&eacute;mographiques&nbsp;&raquo;,
dans le champ &laquo;&nbsp;Code
national&nbsp;&raquo;.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.3">6.3.3
	Pr&eacute;nom</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif">
        Le
pr&eacute;nom du patient. Cette donn&eacute;e provient du texte saisi
sur la fiche d&rsquo;enregistrement du patient dans la section
&laquo;&nbsp;Informations d&eacute;mographiques&nbsp;&raquo;, dans le
champ &laquo;&nbsp;Pr&eacute;nom &raquo;.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.4">6.3.4
	Nom</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif">
        Le
nom de famille du patient. Cette donn&eacute;e provient du texte
saisi sur la fiche d&rsquo;enregistrement du patient, dans la section
&laquo;&nbsp;Informations d&eacute;mographiques&nbsp;&raquo;, dans le
champ &laquo;&nbsp;Nom&nbsp;&raquo;.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.5">6.3.5
	Sexe</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le
sexe du patient. Cette donn&eacute;e provient du texte saisi sur la
fiche d&rsquo;enregistrement du patient, dans la section
&laquo;&nbsp;Informations d&eacute;mographiques&nbsp;&raquo;, dans le
champ &laquo;&nbsp;Sexe&nbsp;&raquo;.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.6">6.3.6
	&Acirc;ge</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        L&rsquo;&acirc;ge
du patient en nombre d&rsquo;ann&eacute;es au moment o&ugrave; la
fiche d&rsquo;enregistrement a &eacute;t&eacute; remplie. Cette
donn&eacute;e provient du texte saisi sur la fiche d&rsquo;enregistrement
du patient, dans la section &laquo;&nbsp;Informations
d&eacute;mographiques&nbsp;&raquo;,dans le champ &laquo;&nbsp;Date de
naissance &raquo;. Si la date de naissance est inconnue, le nombre
d&rsquo;ann&eacute;es ne peut pas &ecirc;tre calcul&eacute;.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.7">6.3.7
	Statut</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le
statut du traitement le plus r&eacute;cent du patient durant une
p&eacute;riode donn&eacute;e; Il peut s&rsquo;agir de &laquo;&nbsp;ARV&nbsp;&raquo;
ou de &laquo;&nbsp;Soins palliatifs&nbsp;&raquo; selon si le patient
a d&eacute;j&agrave; re&ccedil;u ou non des ARV au sein de
l&rsquo;&eacute;tablissement qui effectue le rapport.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="6.3.8">6.3.8
	Derni&egrave;re date</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        La
date de visite la plus r&eacute;cente du patient durant une p&eacute;riode
donn&eacute;e. Ces &eacute;l&eacute;ments de donn&eacute;es
proviennent des dates de visite saisies dans l&rsquo;EMR, &agrave;
partir des fiches de patients.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
