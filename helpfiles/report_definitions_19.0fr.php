	<P CLASS="western" STYLE="margin-bottom: 0in"><BR><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="19.3.1">19.3.1
	&Eacute;ventualit&eacute; de duplication d'enregistrement de patients</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Patients
pour lesquels plus d&rsquo;une fiche d&rsquo;enregistrement peut
avoir &eacute;t&eacute; compl&eacute;t&eacute;e. Les r&eacute;sultats
de ce rapport affichent les informations d&eacute;mographiques des
fiches qui ont pu &ecirc;tre dupliqu&eacute;es permettant, si
n&eacute;cessaire, de les corriger manuellement. Ces donn&eacute;es
proviennent de toutes les fiches saisies dans un &eacute;tablissement
sp&eacute;cifique. Les champs suivants de chaque fiche
d&rsquo;enregistrement sont compar&eacute;s entre eux et les patients
dont les donn&eacute;es s&rsquo;av&egrave;rent identiques sont list&eacute;s
dans le rapport de r&eacute;sultats&nbsp;:&nbsp;
	</SPAN></FONT></P>
	<UL>
		<LI STYLE="list-style: disc outside;margin-left: 25px"><FONT FACE="Arial, sans-serif">Pr&eacute;nom</FONT>
		<LI STYLE="list-style: disc outside;margin-left: 25px"><FONT FACE="Arial, sans-serif">Nom</FONT>
		<LI STYLE="list-style: disc outside;margin-left: 25px"><FONT FACE="Arial, sans-serif">Date de naissance</FONT>
		<LI STYLE="list-style: disc outside;margin-left: 25px"><FONT FACE="Arial, sans-serif">Pr&eacute;nom de la m&egrave;re</FONT>
	</UL>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="19.3.2">19.3.2
	Fiches comportant des erreurs (rapport r&eacute;capitulatif)</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Les
fiches qui comportent des &eacute;l&eacute;ments qui ne respectent
pas les crit&egrave;res de validation de champ. Le rapport de
r&eacute;sultats affiche les &eacute;l&eacute;ments de donn&eacute;es
