</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="19.0">19.0
	Qualit&eacute; des donn&eacute;e&nbsp;:&nbsp;&Eacute;purage des donn&eacute;es</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Ces
rapports englobent tous les patients, quelque soit leur statut
d&rsquo;activit&eacute; et de traitement et sont destin&eacute;s &agrave;
produire des r&eacute;sultats individuels dans les diverses
cat&eacute;gories o&ugrave; des donn&eacute;es sont non valides sur
des fiches sp&eacute;cifiques de patient.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="19.1">19.1
	Utilisation</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Afin
d&rsquo;utiliser ces rapports, veuillez simplement cliquer sur le
lien du titre de rapport. La page de param&egrave;tres de rapport ne
s&rsquo;applique pas &agrave; ces rapports.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="19.2">19.2
	R&eacute;sultats</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Les
r&eacute;sultats du rapport apparaissent dans une nouvelle fen&ecirc;tre
de navigateur ou un nouvel onglet, suivant la mani&egrave;re dont le
navigateur est configur&eacute;. Un exemple de l&rsquo;en-t&ecirc;te
des r&eacute;sultats est illustr&eacute; par la figure 21 dans la
section
	<A HREF="#19.2.1">19.2.1</A> </FONT><FONT FACE="Arial, sans-serif"><I>(ci-dessous)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="19.2.1"><IMG SRC="images/report_definitions_html_fr_7f464ab3.jpg" NAME="graphics21" ALIGN=BOTTOM WIDTH=800 HEIGHT=82 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>19.2.1
	Figure 21 : En-t&ecirc;te de r&eacute;sultats pour le rapport &laquo;&nbsp;Fiches comportant des erreurs&nbsp;&raquo;</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Il
existe 2 boutons de commande au dessus du rapport de r&eacute;sultats.
Le bouton &laquo;&nbsp;Fermer&nbsp;&raquo;
ferme la fen&ecirc;tre de r&eacute;sultats (onglet). Le bouton
&laquo;&nbsp;excel&nbsp;&raquo; g&eacute;n&egrave;re une version
alternative du m&ecirc;me rapport de r&eacute;sultats sous le format
CSV.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Le
r&eacute;capitulatif des param&egrave;tres, se trouve sous les deux
boutons de commande. Chacune des colonnes peut &ecirc;tre tri&eacute;e
par ordre croissant ou d&eacute;croissant, en cliquant sur la fl&egrave;che
descendante (&agrave; gauche) ou la fl&egrave;che ascendante (&agrave;
droite), au niveau du titre de colonne.
	</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="19.3">19.3
	D&eacute;finitions importantes</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_6.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#6.3">6.3 &laquo;&nbsp;D&eacute;finitions importantes&nbsp;&raquo;</A> ci-dessus pour les d&eacute;finitions de la colonne intitul&eacute;e
