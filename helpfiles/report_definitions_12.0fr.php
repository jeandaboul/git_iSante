<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=6 STYLE="font-size: 22pt"><B>Rapport de d&eacute;finition EMR</B></FONT></FONT></FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="12.0">12.0
	Qualit&eacute; des soins&nbsp;:&nbsp;Indicateurs d'analyses de laboratoires n&eacute;cessaires</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">
        Ces
rapports indiquent le niveau de performance de qualit&eacute; des
indicateurs de soins. Par exemple, un rapport r&eacute;trospectif de
test CD4 peut montrer la proportion de patients actifs sous ARV pour
laquelle un compte CD4 a &eacute;t&eacute; prescrit et effectu&eacute;
dans les 8 mois pr&eacute;c&eacute;dents. Les rapports mesurent les
niveaux de performance sous des objectifs fixes, les faiblesses, ou
aussi indiquer un besoin d&rsquo;am&eacute;liorer la documention ou
les besoins de contr&ocirc;le. Des rapports indiquant une performance
m&eacute;diocre peut en plus indiquer le besoin d&rsquo;une
&eacute;valuation suppl&eacute;mentaire par le biais d&rsquo;autres
m&eacute;chanismes, par exemple la r&eacute;vision des donn&eacute;es
individuelles de patients.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="12.1">12.1
	Utilisation</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Afin
d&rsquo;utiliser ces rapports, veuillez simplement s&eacute;lectionner
un ou plusieurs type(s) de statut de patient dans la section du
rapport &laquo;&nbsp;page de param&egrave;tres &raquo;, puis
s&eacute;lectionner type de test dans la section &laquo;&nbsp;Type de
test&nbsp;&raquo;, et tout autre crit&egrave;re &agrave; utiliser sur cette
page, afin de g&eacute;n&eacute;rer les r&eacute;sultats de rapport
souhait&eacute;s
	<I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_5.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#5.0">5.0 &laquo;&nbsp;Page de param&egrave;tres de rapport&nbsp;&raquo;</A>, ci-dessus pour les d&eacute;finitions des
sections de la page de param&egrave;tres du rapport).</I> Cliquez ensuite sur le bouton &laquo;&nbsp;Soumettre&nbsp;&raquo;.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><B>Note&nbsp;:&nbsp;</B></FONT><FONT FACE="Arial, sans-serif">
        Les
champs &laquo;&nbsp;Date de d&eacute;but&nbsp;&raquo; et &laquo;&nbsp;Date de fin&nbsp;&raquo; ne peuvent pas &ecirc;tre
s&eacute;lectionn&eacute;s dans ces rapports puisque les r&eacute;sultats
sont suppos&eacute;s couvrir l&rsquo;historique des donn&eacute;es
dans l&rsquo;&eacute;tablissement qui effectue le rapport. Le rapport
&laquo;&nbsp;Test &agrave; jour&nbsp;&raquo;
limite la liste des tests pouvant &ecirc;tre s&eacute;lectionn&eacute;s
au CD4, bilan h&eacute;patique et tests CBC.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="12.2">12.2
	R&eacute;sultats</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Les
r&eacute;sultats du rapport apparaissent dans une nouvelle fen&ecirc;tre
de navigateur ou un nouvel onglet, suivant la mani&egrave;re dont le
navigateur est configur&eacute;. Un exemple de l&rsquo;en-t&ecirc;te
des r&eacute;sultats est illustr&eacute; par la figure 13 dans la
section
	<A HREF="#12.2.1">12.2.1</A> </FONT><FONT FACE="Arial, sans-serif"><I>(ci-dessous)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="12.2.1"><IMG SRC="images/report_definitions_html_fr_m195a4a6b.jpg" NAME="graphics13" ALIGN=BOTTOM WIDTH=800 HEIGHT=369 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>12.2.1
	Figure 13 : En-t&ecirc;te de r&eacute;sultats pour le
rapport &laquo;&nbsp;Test effectu&eacute;&nbsp;&raquo;</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Il existe 3 boutons de commande au sommet des r&eacute;sultats du
rapport. Le bouton &laquo;&nbsp;Fermer&nbsp;&raquo;
ferme la fen&ecirc;tre de r&eacute;sultats (onglet). Les boutons
&laquo;&nbsp;pdf&nbsp;&raquo; et &laquo;&nbsp;excel&nbsp;&raquo;
g&eacute;n&egrave;rent des versions alternatives du m&ecirc;me
rapport de r&eacute;sultats, soit respectivement sous PDF ou sous
CSV.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Le r&eacute;capitulatif des param&egrave;tres utilis&eacute;s pour
produire le rapport, ainsi sp&eacute;cifi&eacute;s dans la page de
param&egrave;tres de rapport, se situe au dessous des 3 boutons de
commande. L&rsquo;en-t&ecirc;te de r&eacute;sultats se situe sous le
r&eacute;capitulatif des param&egrave;tres de rapport. Chacune des
colonnes peut &ecirc;tre tri&eacute;e par ordre croissant ou
d&eacute;croissant, en cliquant sur la fl&egrave;che descendante (&agrave;
gauche) ou la fl&egrave;che ascendante (&agrave; droite), au niveau du
titre de colonne.
	</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="12.3">12.3
	D&eacute;finitions importantes</A></B></FONT></FONT><BR><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="12.3.1">12.3.1
	P&eacute;riode de temps</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        La p&eacute;riode de temps de 6 mois couvrant toutes les donn&eacute;es
disponibles dans l&rsquo;&eacute;tablissement clinique s&eacute;lectionn&eacute;.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="12.3.2">12.3.2
	Num&eacute;rateur</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Pour le rapport &laquo;&nbsp;Tests d&eacute;j&agrave; effectu&eacute;s&nbsp;&raquo;, le
nombre de patients dans l&rsquo;&eacute;tablissement s&eacute;lectionn&eacute;
dont un r&eacute;sultat a d&eacute;j&agrave; &eacute;t&eacute; saisi
pour le test indiqu&eacute; sur une fiche de pharmacie de ce m&ecirc;me
&eacute;tablissement. Ces donn&eacute;es proviennent des champs de
r&eacute;sultats pour les tests indiqu&eacute;s sur les fiches de
laboratoire.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Pour
le rapport &laquo;&nbsp;Test &agrave; jour&nbsp;&raquo;, le nombre de patients qui sont &agrave;
jour dans l&rsquo;&eacute;tablissement s&eacute;lectionn&eacute; pour
le test indiqu&eacute;. Les patients, &laquo;&nbsp;&agrave; jour&nbsp;&raquo;
sont d&eacute;finis comme ayant eu un r&eacute;sultat de test dans
les 8 derniers mois. Ces donn&eacute;es proviennent des champs de
r&eacute;sultats des tests indiqu&eacute;s sur toutes les fiches de
laboratoire.
        </FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="12.3.3">12.3.3
	D&eacute;nominateur</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le
nombre total de patients dans un &eacute;tablissement s&eacute;lectionn&eacute;
qui ont eu au moins une visite dans la p&eacute;riode de temps
s&eacute;lectionn&eacute;e. Ces donn&eacute;es proviennent de toutes
les fiches d&rsquo;enregistrement de patients au sein de
l&rsquo;&eacute;tablissement s&eacute;lectionn&eacute;.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="12.3.4">12.3.4
	Pourcentage</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Ces donn&eacute;es sont calcul&eacute;es en divisant le num&eacute;rateur par le d&eacute;nominateur.
	 </SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
