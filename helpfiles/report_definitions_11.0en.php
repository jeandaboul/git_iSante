	Drug substitution and regimen switching</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in">The next two reports
	will display a list of patients who have had a change in regimen
	during the specified time frame.</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in">The report titled
	'Patients with one drug substitution on the same regimen line'
	displays a list of patients who have had a change of one drug within
	the same line of therapy.  For example, a patient who started on
	AZT-3TC-EFV and later switched to AZT-3TC-NVP would be displayed in
	this report, but not a patient who started on AZT-3TC-EFV and later
	switched to d4T-3TC-NVP.</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in">The report titled
	'Patients with more than one drug substitution on the same regimen
	line, or a new regimen line' displays a list of patients who have
	had a change of more than one drug within the same line of therapy
	or who have switched to a different line of therapy.  For example, a
	patient who started on AZT-3TC-EFV and later switched to d4T-3TC-NVP
	would be displayed in this report, but not a patient who started on
	AZT-3TC-EFV and later switched to AZT-3TC-NVP.<BR><BR>
