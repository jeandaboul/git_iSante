<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=6 STYLE="font-size: 22pt"><B>Rapport de d&eacute;finition EMR</B></FONT></FONT></FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="5.0">5.0
	Page de param&egrave;tres de rapport</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">
        Plusieurs
rapports peuvent &ecirc;tre filtr&eacute;s ou d&eacute;velopp&eacute;s
avec des graphiques bas&eacute;s sur des param&egrave;tres tels que
le statut d'activit&eacute; du patient, le regroupement
d&eacute;mographique, le statut de traitement, le niveau
d'organisation, etc. La page de param&egrave;tres de rapport
	</FONT><FONT FACE="Arial, sans-serif"><I>(Voir la figure 1 de la section <A HREF="#5.0.1">5.0.1</A>
	ci-dessous)</I></FONT><FONT FACE="Arial, sans-serif"> 
         est utilis&eacute;e pour
d&eacute;terminer les filtres et les d&eacute;tails de mise en forme
du rapport.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><A NAME="5.0.1"><IMG SRC="images/report_definitions_html_fr_m2f9392de.jpg" NAME="graphics1" ALIGN=BOTTOM WIDTH=800 HEIGHT=377 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>5.0.1
	Figure 1 : Page de param&egrave;tres de rapport pour le
rapport &laquo;&nbsp;Test effectu&eacute;&nbsp;&raquo;</I></FONT></FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">
        Pour
certains rapports, des sections de la page de param&egrave;tres
peuvent &ecirc;tre d&eacute;sactiv&eacute;es (estomp&eacute;es) parce
qu'elles ne sont pas appropri&eacute;es &agrave; un rapport
sp&eacute;cifique. Dans la plupart des cas, la valeur des param&egrave;tres
seront automatiquement pr&eacute;d&eacute;finis de mani&egrave;re la
plus appropri&eacute;e au rapport. Pour lancer le rapport, cliquez
simplement sur le bouton &laquo;&nbsp;Soumettre&nbsp;&raquo; au bas de la page.
	</FONT>
	</P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="5.1">5.1
	D&eacute;finitions importantes</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="5.1.1">5.1.1
	Dates de d&eacute;but/fin</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        La p&eacute;riode du
calcul des r&eacute;sultats du rapport. Chaque date doit &ecirc;tre
entr&eacute;e sous la forme MM/AA (2 chiffres pour le mois/ 2
chiffres pour l&rsquo;ann&eacute;e). La date r&eacute;elle utilis&eacute;e
lors du calcul du rapport est le dernier jour du mois sp&eacute;cifi&eacute;
dans le champ de date. Par exemple, si la &laquo;&nbsp;Date de
d&eacute;but&nbsp;&raquo; est &laquo;&nbsp;02/04&nbsp;&raquo; et la &laquo;&nbsp;Date de fin&nbsp;&raquo; est &laquo;&nbsp;08/06&nbsp;&raquo;, la p&eacute;riode de calcul s&rsquo;&eacute;tendra du 29/02/2004 au 31/08/2006.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="5.1.2">5.1.2
	Statut de patient</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le
type de patients &agrave; inclure dans les r&eacute;sultats de
rapport
	<FONT COLOR="#000000"><FONT FACE="Arial, sans-serif"><I><SPAN STYLE="font-weight: medium">(Voir la section <A HREF="helpfile.php?file=report_definitions_4.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>">4.0
	&laquo;&nbsp;Classification des patients par statut d&rsquo;activit&eacute;&nbsp;&raquo;</A>, ci-dessus pour les d&eacute;finitions de statut d&rsquo;activit&eacute; de patient)
	</SPAN></I></FONT></FONT>.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="5.1.3">5.1.3
	Niveau organisationnel</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le
groupe de patients pour lesquels le rapport doit &ecirc;tre effectu&eacute;.
Lorsque &laquo;&nbsp;Patients&nbsp;&raquo;
est choisi, le rapport contient une ligne de r&eacute;sultat pour
chaque patient de l&rsquo;&eacute;tablissement affich&eacute; qui
correspond aux crit&egrave;res du rapport. Lorsque &laquo;&nbsp;Clinique&nbsp;&raquo;,
&laquo;&nbsp;Commune&nbsp;&raquo;, &laquo;&nbsp;D&eacute;partement&nbsp;&raquo;
ou &laquo;&nbsp;R&eacute;seau&nbsp;&raquo;
est choisi, le nombre de patients qui correspond aux crit&egrave;res du
rapport est affich&eacute; avec un graphique de r&eacute;sultats,
sous forme de sous-groupes appropri&eacute;s.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="5.1.4">5.1.4
	Le statut du traitement</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif">
        Le type de traitements de patients qui doivent &ecirc;tre inclus dans
le calcul des r&eacute;sultats du rapport. Si une option diff&eacute;rente
de &laquo;&nbsp;Tout statut&nbsp;&raquo;
est choisie, uniquement les patients qui re&ccedil;oivent ce type de
traitements seront inclus lors du calcul des r&eacute;sultats du
rapport. Les types de traitements sont d&eacute;finies ci-dessous&nbsp;:&nbsp;
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
	</P>
	<UL>
		<UL>
			<LI STYLE="list-style: disc outside;margin-left: 25px"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="5.1.4.1">5.1.4.1
			Sous ARV</A></I></FONT></FONT><FONT FACE="Arial, sans-serif">&nbsp;:&nbsp; 
                        Patients
                qui ont re&ccedil;u durant une p&eacute;riode sp&eacute;cifique un
                traitement ARV leur &eacute;tant destin&eacute;, &agrave; l'&rsquo;exception des patients qui ont re&ccedil;u des ARV pour
                la pr&eacute;vention de transmission de m&egrave;re &agrave;
                enfant (PTME) ou prophylaxie post-exposition (PPE).
			</FONT>
		</UL>
	</UL>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
	</P>
	<UL>
		<UL>
			<LI STYLE="list-style: disc outside;margin-left: 25px"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="5.1.4.2">5.1.4.2
			Sous soins palliatifs</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif">
                        Patients qui n&rsquo;ont jamais re&ccedil;u durant une p&eacute;riode
                sp&eacute;cifique un traitement ARV leur &eacute;tant
                destin&eacute;,
                ou qui ont re&ccedil;u des ARV pour
                la pr&eacute;vention de
                transmission de m&egrave;re &agrave; enfant (PTME) ou prophylaxie post-exposition (PPE).
			</FONT>
		</UL>
	</UL>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
	</P>
	<UL>
		<UL>
			<LI STYLE="list-style: disc outside;margin-left: 25px"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="5.1.4.3">5.1.4.3
			Sous prophylaxie &agrave; l&rsquo;INH</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><B>
			</B></FONT><FONT FACE="Arial, sans-serif">
                        Patients
                qui ont re&ccedil;u durant une p&eacute;riode sp&eacute;cifique une
                dose d&rsquo;Isoniazide sans prescription suppl&eacute;mentaire de
                m&eacute;dicament anti-TB (Ethambutol, Pyrazinamide, Rifampicine, Streptomycine)
			</FONT>
		</UL>
	</UL>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
	</P>
	<UL>
		<UL>
			<LI STYLE="list-style: disc outside;margin-left: 25px"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="5.1.4.4">5.1.4.4
			Sous Cotrimoxazole</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
                        Patients
                qui ont re&ccedil;u durant une p&eacute;riode sp&eacute;cifique une dose de Cotrimoxazole.
			</SPAN></FONT>
		</UL>
	</UL>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
	</P>
	<UL>
		<UL>
			<LI STYLE="list-style: disc outside;margin-left: 25px"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="5.1.4.5">5.1.4.5
			Sous traitement TB</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
                        Patients
                qui ont re&ccedil;u durant une p&eacute;riode sp&eacute;cifique une
                dose simumultan&eacute;e d&rsquo;au moins 3 des m&eacute;dicaments
                suivants &nbsp;:&nbsp;Ethambutol, Isoniazid,
                Pyrazinamide, Rifampicine, Streptomycine.
			</SPAN></FONT>
		</UL>
	</UL>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="5.1.5">5.1.5
	Groupement d&eacute;mographique</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        En
suppl&eacute;ment des r&eacute;sultats du rapport, le type de
regroupement d&eacute;mographique &agrave; utiliser dans le graphique
incorpor&eacute; doit &ecirc;tre pr&eacute;cis&eacute;. Si l&rsquo;option
&laquo;&nbsp;Aucun&nbsp;&raquo;
est choisie, aucun graphique ne sera produit. Si l&rsquo;option
&laquo;&nbsp;Sexe&nbsp;&raquo;
est choisie, le graphique en camembert inclus au sommet des r&eacute;sultats
du rapport sera trac&eacute; en fonction du sexe des patients qui
correspondent aux crit&egrave;res du rapport. Si l&rsquo;option
&laquo;&nbsp;&Acirc;ge&nbsp;&raquo;
est choisie, le graphique en camembert inclus au sommet des r&eacute;sultats
de rapport sera trac&eacute; en fonction des &acirc;ges des patients
qui correspondent aux crit&egrave;res du rapport. Si l&rsquo;option
&laquo;&nbsp;Grossesse&nbsp;&raquo;
est choisie, le graphique en camembert inclus au sommet des r&eacute;sultats
du rapport sera trac&eacute; en fonction du statut de grossesse des
patients femmes qui sont &acirc;g&eacute;es de plus de 14 ans et qui
correspondent aux crit&egrave;res du rapport.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><I><A NAME="5.1.6">5.1.6
	Type de Tests</A></I></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Le type de tests de laboratoire qui doit &ecirc;tre inclus dans les
r&eacute;sultats du rapport. Les r&eacute;sultats du rapport seront
limit&eacute;s au type de tests de laboratoire s&eacute;lectionn&eacute;.
	</SPAN></FONT></P>
