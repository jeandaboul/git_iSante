	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">
        Chaque
cat&eacute;gorie de rapports est d&eacute;crite avec de plus amples
d&eacute;tails, ci-apr&egrave;s. Le niveau d&rsquo;agr&eacute;gation
de donn&eacute;es est repr&eacute;sent&eacute; par le tableau,
ci-dessus
	</FONT><FONT FACE="Arial, sans-serif"><I>(Voir <A HREF="#3.0.1">&laquo; 3.0.1 Tableau 1 &raquo;</A>)</I></FONT><FONT FACE="Arial, sans-serif">
         Les rapports
individuels de patients permettent de visualiser les donn&eacute;es
m&eacute;dicales primaires d&rsquo;un patient. L&rsquo;agr&eacute;gation
d&rsquo;un &eacute;tablissement m&eacute;dical unique r&eacute;capitule
les donn&eacute;es de tous les patients VIH au sein d&rsquo;un
&eacute;tablissement donn&eacute;. Les regroupements nationaux
comprennent des regroupements g&eacute;ographiques (communes et
d&eacute;partements), des regroupements de r&eacute;seaux et de
serveurs. Ces regroupements au niveau national servent uniquement aux
instances o&ugrave; le rapport est lanc&eacute; sur un serveur
consolid&eacute; qui comporte les donn&eacute;es agr&eacute;g&eacute;es
de plusieurs &eacute;tablissements.
