<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="13.0">13.0
	Quality of Care: Appropriate treatment indicators</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">These
	reports are similar to the reports in the 'Appropriate lab test
	indicators' category <I>(see section <A HREF="helpfile.php?file=report_definitions_12.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>&">12.0 Quality of Care:
	Appropriate lab test indicators</A> above)</I><SPAN STYLE="font-style: normal">
	but are related to treatment groups as opposed to particular lab
	test results.  </SPAN><SPAN STYLE="font-style: normal">The reports
	in this category indicate total percentages of total eligible
	patients that were in certain treatment groups, such as
	Cotrimoxazole prophylaxis or TB treatment, during six-month
	intervals for all available data at the reporting clinic site.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="13.1">13.1
	Usage</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">To
	use these reports, simply select one or more patient status groups
	from the 'Patient Status' section of the report parameters page and
	select any other criteria on that page to be used in generating the
	report results that are desired </FONT><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_5.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#5.0">5.0 Report Parameters Page</A> above </I></FONT><FONT FACE="Arial, sans-serif"><I>for
	definitions of the sections on the report parameters page)</I></FONT><FONT FACE="Arial, sans-serif">.
	 Then, click the 'Submit' button.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><B>Note:</B></FONT><FONT FACE="Arial, sans-serif">
	The 'Start Date' and 'End Date' fields are not selectable in these
	reports since the results are meant to cover the entire history of
	data at the reporting clinic site.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="13.2">13.2
	Results</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">The
	report results will appear in a new browser window (or a new browser
	tab, depending on </FONT><FONT FACE="Arial, sans-serif">how your
	browser is configured).  An example results header is shown in
	<A HREF="#13.2.1">13.2.1 Figure 14</A> </FONT><FONT FACE="Arial, sans-serif"><I>(below)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="13.2.1"><IMG SRC="images/report_definitions_html_2043542e.jpg" NAME="graphics14" ALIGN=BOTTOM WIDTH=800 HEIGHT=388 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>13.2.1
	Figure 14: Results header for the 'ART Enrollment Among Medically
	Eligible' report</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">There
	are three action buttons at the top of the report results.  The
	'Close' button will close the report results window (or tab).  The
	'pdf' and 'excel' buttons will generate alternate versions of the
	same report results in either PDF or CSV formats respectively.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">Below
	the three action buttons is a summary of the parameters used to
	generate the report results as provided on the report parameters
	page.  Underneath the parameter summary is a chart displaying the
	results by percentage over the entire time period broken into
	six-month intervals for the selected clinic site.  Beneath the chart
	is the results header.  Each of the columns can be sorted in
	ascending or descending order by clicking on the down arrow (left
	side) or up arrow (right side) next to the column title.</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="13.3">13.3
	Relevant Definitions</A></B></FONT></FONT><BR><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_12.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#12.3">12.3 Relevant Definitions</A> above for definitions of the
	columns titled 'Time Period' and 'Percentage').</I></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="13.3.1">13.3.1
	Numerator</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
	 The number of patients at the selected clinic site who meet the
	criteria for being included in the treatment group for the
	particular report.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">For
	the 'ART Enrollment Among Medically Eligible' report, the number of
	patients who have been enrolled in ART at the clinic </SPAN></FONT><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_9.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#9.1.3">9.1.3 Relevant Definitions</A> above for the derivation of
	medical eligibility for ART and ART enrollment)</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">For
	the 'Cotrimoxazole prophylaxis among medically eligible' report, the
	number of patients </SPAN></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">who
	have received Cotrimoxazole prophylaxis </SPAN></FONT><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_9.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#9.2.3">9.2.3 Relevant Definitions</A> above for the derivation of
	Cotrimoxazole eligibility and appropriate therapy)</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">For
	the 'Continuation Cotrimoxazole prophylaxis' report, the number of
	patients, regardless of ART status, who initiated Cotrimoxazole
	prophylaxis and also received an average of at least six dispensed
	doses over the prior six month period. </SPAN></FONT><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_9.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#9.3.3">9.3.3 Relevant Definitions</A> above for the derivation of
	Cotrimoxazole initiation)</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">For
	the 'TB treatment among medically eligible' report, the number of
	patients who have completed TB treatment. </SPAN></FONT><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_10.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#10.3">10.3 Relevant Definitions</A> above for the derivation of TB
	treatment)</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">For
	the 'Continuation TB treatment' report, the number of patients who
	are currently undergoing TB treatment. </SPAN></FONT><FONT FACE="Arial, sans-serif"><I>(see
	section <A HREF="helpfile.php?file=report_definitions_10.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20D%e9finitions&lang=<?php echo $lang;?>#10.3">10.3 Relevant Definitions</A> above for the derivation of TB
	treatment)</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">.</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="13.3.2">13.3.2
	Denominator</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>:</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
