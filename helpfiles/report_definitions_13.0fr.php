</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="13.0">13.0
	Qualit&eacute; des soins&nbsp;:&nbsp;Indicateurs des soins n&eacute;cessaires</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">
        Ces
rapports sont semblables aux rapports de la cat&eacute;gorie
&laquo;&nbsp;Indicateurs de tests de laboratoire n&eacute;cessaires&nbsp;&raquo;
	<I>(Voir la section <A HREF="helpfile.php?file=report_definitions_12.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>&">12.0 &laquo;&nbsp;Qualit&eacute; des soins&nbsp;:&nbsp;Indicateurs d'analyses de laboratoires n&eacute;cessaires&nbsp;&raquo;</A> ci-dessus)</I><SPAN STYLE="font-style: normal">
         mais sont destin&eacute;s
aux r&eacute;sultats des types de traitements plut&ocirc;t qu&rsquo;&agrave;
un test de laboratoire sp&eacute;cifique. Les rapports de cette
cat&eacute;gorie indiquent le pourcentage total de patients &eacute;ligibles
au sein de groupes de traitement sp&eacute;cifiques, comme la
prophylaxie &agrave; la Cotrimoxazole ou le traitement contre la TB,
durant une p&eacute;riode de 6 mois pour toutes les donn&eacute;es
disponibles dans l&rsquo;&eacute;tablissement qui effectue le
rapport.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="13.1">13.1
	Utilisation</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Afin
d&rsquo;utiliser ces rapports, veuillez simplement s&eacute;lectionner
un ou plusieurs type(s) de statut de patient dans la section du
rapport &laquo;&nbsp;page de param&egrave;tres &raquo;, et tout autre
crit&egrave;re &agrave; utiliser sur cette page, afin de g&eacute;n&eacute;rer
les r&eacute;sultats de rapport souhait&eacute;s
	<I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_5.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#5.0">5.0 &laquo;&nbsp;Page de param&egrave;tres de rapport&nbsp;&raquo;</A>,
ci-dessus pour les d&eacute;finitions des sections de la page de
param&egrave;tres du rapport)</I></FONT><FONT FACE="Arial, sans-serif">.
	 Cliquez ensuite sur le bouton
&laquo;&nbsp;Soumettre&nbsp;&raquo;.</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><B>Note&nbsp;:&nbsp;</B></FONT><FONT FACE="Arial, sans-serif">
        Les
champs &laquo;&nbsp;Date de d&eacute;but&nbsp;&raquo;
et &laquo;&nbsp;Date de fin&nbsp;&raquo; ne peuvent pas &ecirc;tre s&eacute;lectionn&eacute;s
dans ces rapports puisque les r&eacute;sultats ne sont pas suppos&eacute;s
couvrir l&rsquo;historique compl&egrave;te des donn&eacute;es dans
l&rsquo;&eacute;tablissement qui effectue le rapport.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="13.2">13.2
