de navigateur ou un nouvel onglet, suivant la fa&ccedil;on dont le
navigateur est configur&eacute;. Un exemple de l&rsquo;en-t&ecirc;te
des r&eacute;sultats est illustr&eacute; par la figure 14, dans la
section
	<A HREF="#13.2.1">13.2.1</A> </FONT><FONT FACE="Arial, sans-serif"><I>(ci-dessous)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
