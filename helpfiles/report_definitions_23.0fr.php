	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Ce
rapport peut &ecirc;tre utilis&eacute; pour g&eacute;n&eacute;rer un
r&eacute;capitulatif m&eacute;dical ou un historique des soins de
patient au sein de l&rsquo;&eacute;tablissement m&eacute;dical. Il a
