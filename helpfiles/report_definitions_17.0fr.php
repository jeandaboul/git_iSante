<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=6 STYLE="font-size: 22pt"><B>Rapport de d&eacute;finition EMR</B></FONT></FONT></FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0in"><BR>
</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=4 STYLE="font-size: 16pt"><B><A NAME="17.0">17.0
	Qualit&eacute; des donn&eacute;es&nbsp;:&nbsp;Donn&eacute;es manquantes</A></B></FONT></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif">
        Les
rapports de qualit&eacute; de donn&eacute;es ont pour but d&rsquo;&ecirc;tre
utilis&eacute;s par les op&eacute;rateurs de saisi et les
gestionnaires de donn&eacute;es afin d&rsquo;identifier les donn&eacute;es
manquantes ou invalides et de g&eacute;rer le proc&eacute;d&eacute;
d&rsquo;&eacute;purage de donn&eacute;es. Ces rapports englobent tous
les patients, quelque soit leur statut d&rsquo;activit&eacute; et de
traitement et sont destin&eacute;s &agrave; produire des r&eacute;sultats
individuels dans les diverses cat&eacute;gories o&ugrave; il manque
des donn&eacute;es sur des fiches sp&eacute;cifiques de patient.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="17.1">17.1
	Utilisation</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Afin
d&rsquo;utiliser ces rapports, veuillez simplement cliquer sur le
lien du titre de rapport. La page de param&egrave;tres de rapport ne
s&rsquo;applique pas &agrave; ces rapports.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="17.2">17.2
	R&eacute;sultats</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Les
r&eacute;sultats du rapport apparaissent dans une nouvelle fen&ecirc;tre
de navigateur ou un nouvel onglet, suivant la mani&egrave;re dont le
navigateur est configur&eacute;. Un exemple de l&rsquo;en-t&ecirc;te
des r&eacute;sultats est illustr&eacute; par la figure 19 dans la
section
	<A HREF="#17.2.1">17.2.1</A> </FONT><FONT FACE="Arial, sans-serif"><I>(ci-dessous)</I></FONT><FONT FACE="Arial, sans-serif">.
	 </FONT>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><A NAME="17.2.1"><IMG SRC="images/report_definitions_html_fr_m148f868e.jpg" NAME="graphics19" ALIGN=BOTTOM WIDTH=766 HEIGHT=80 BORDER=0></A></P>
	<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 9pt"><I>17.2.1
	Figure 19 : En-t&ecirc;te de r&eacute;sultats pour le rapport &laquo;&nbsp;Patients sans d&eacute;signation de sexe&nbsp;&raquo;</I></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Il
existe 2 boutons de commande au dessus du rapport de r&eacute;sultats.
Le bouton &laquo;&nbsp;Fermer&nbsp;&raquo;
ferme la fen&ecirc;tre de r&eacute;sultats (onglet). Le bouton
&laquo;&nbsp;excel&nbsp;&raquo; g&eacute;n&egrave;re une version
alternative du m&ecirc;me rapport de r&eacute;sultats sous le format
CSV.
	</FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif">
        Le
r&eacute;capitulatif des param&egrave;tres, se trouve sous les deux
boutons de commande. Chacune des colonnes peut &ecirc;tre tri&eacute;e
par ordre croissant ou d&eacute;croissant, en cliquant sur la fl&egrave;che
descendante (&agrave; gauche) ou la fl&egrave;che ascendante (&agrave;
droite), au niveau du titre de colonne.
	</FONT></P>
	<P CLASS="western" ALIGN=LEFT STYLE="margin-bottom: 0in"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><B><A NAME="17.3">17.3
	D&eacute;finitions importantes</A></B></FONT></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT FACE="Arial, sans-serif"><I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_6.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#6.3">6.3 &laquo;&nbsp;D&eacute;finitions importantes&nbsp;&raquo;</A> 
	ci-dessus pour les d&eacute;finitions des colonnes intitul&eacute;es
&laquo;&nbsp;No. de patient attribu&eacute; par le site&nbsp;&raquo;,
&laquo;&nbsp;Num&eacute;ro d&rsquo;identit&eacute; nationale&nbsp;&raquo;,
&laquo;&nbsp;Pr&eacute;nom&nbsp;&raquo;
et &laquo;&nbsp;Nom&nbsp;&raquo;)</I></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><BR><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="17.3.1">17.3.1
	Patients sans d&eacute;signation de sexe</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Les
patients dont le sexe n&rsquo;est pas sp&eacute;cifi&eacute;. Le sexe
est une variable obligatoire pour les rapports mensuels
d&rsquo;&eacute;tablissement PEPFAR. Ces donn&eacute;es proviennent
du manque de s&eacute;lection de l&rsquo;option &laquo;&nbsp;F&nbsp;&raquo;
ou &laquo;&nbsp;H&nbsp;&raquo; dans le champ intitul&eacute; &laquo;&nbsp;Sexe&nbsp;&raquo;
sur les fiches de premi&egrave;re visite de patients dans la section
intitul&eacute;e &laquo;&nbsp;Informations d&eacute;mographiques&nbsp;&raquo;.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="17.3.2">17.3.2
	Patients sans sp&eacute;cification d&rsquo;ann&eacute;e de naissance ou d&rsquo;&acirc;ge</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
Patients
sans ann&eacute;e de naissance valide (2 chiffres) ou d&rsquo;&acirc;ge
saisi. L&rsquo;ann&eacute;e de naissance et l&rsquo;&acirc;ge sont
utilis&eacute;s pour calculer l&rsquo;&acirc;ge actuel du patient,
variable n&eacute;cessaire pour les rapports mensuels d&rsquo;&eacute;tablissement
PEPFAR. Ces donn&eacute;es proviennent de l&rsquo;absence de valeur
sur la fiche de premi&egrave;re visite du patient dans la section
intitul&eacute;e &laquo;&nbsp;Informations d&eacute;mographiques&nbsp;&raquo;
pour les champs intitul&eacute;s &laquo;&nbsp;Date de naissance&nbsp;&raquo;
et &laquo;&nbsp;Si date inconnu, &acirc;ge en ann&eacute;es&nbsp;&raquo;.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="17.3.3">17.3.3
	Patients sans no. d&rsquo;&eacute;tablissement</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Patients
pour lesquels le num&eacute;ro d&rsquo;&eacute;tablissement est
incorrect ou non attribu&eacute;. Ces donn&eacute;es proviennent de
l&rsquo;absence de valeur sur la fiche de premi&egrave;re visite du
patient dans la section intitul&eacute;e &laquo;&nbsp;Informations
d&eacute;mographiques&nbsp;&raquo; dans le champ intitul&eacute; &laquo;&nbsp;No. d&rsquo;&eacute;tablissement&nbsp;&raquo;.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif"><FONT SIZE=3><I><A NAME="17.3.4">17.3.4
	Patients de statut actif avec des fiches de pharmacie compl&eacute;t&eacute;es</A></I></FONT></FONT></FONT><FONT FACE="Arial, sans-serif"><I>&nbsp;:&nbsp;</I></FONT><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal">
        Chaque
nouveau patient actif ou nouveau patient &agrave; risque qui poss&egrave;de
une fiche de pharmacie compl&eacute;t&eacute;e.
	<I>(Voir la
	section <A HREF="helpfile.php?file=report_definitions_4.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#4.0">4.0 &laquo;&nbsp;Classification des patients par statut d&rsquo;activit&eacute;&nbsp;&raquo;</A>
ci-dessus pour les d&eacute;finitions des statuts de patient)</I>.
        Ce rapport peut &ecirc;tre
utilis&eacute; au sein des &eacute;tablissements afin de v&eacute;rifier
les registres papier de pharmacie (cahiers) par rapport aux donn&eacute;es
&eacute;lectroniques du patient, et aider le personnel de pharmacie &agrave;
mettre &agrave; jour le syst&egrave;me EMR avec les prescriptions et
remises de m&eacute;dicaments pour chaque patient. Ces donn&eacute;es
proviennent de toutes les fiches de pharmacie compl&eacute;t&eacute;es
au sein de l&rsquo;&eacute;tablissement m&eacute;dical s&eacute;lectionn&eacute;.
	</SPAN></FONT></P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><BR>
	</P>
	<P CLASS="western" STYLE="margin-bottom: 0in; font-style: normal"><FONT FACE="Arial, sans-serif"><FONT COLOR="#000080"><I><A NAME="17.3.5">17.3.5
	Patients de statut actif avec des fiches de laboratoire compl&eacute;t&eacute;es</A></I></FONT><I>&nbsp;:&nbsp;</I>
        Chaque
nouveau patient actif ou nouveau patient &agrave; risque qui poss&egrave;de
une fiche de laboratoire compl&eacute;t&eacute;e.
	<I>(Voir la section <A HREF="helpfile.php?file=report_definitions_4.0&extension=php&titleen=EMR%20Report%20Definitions&titlefr=Rapport%20de%20d%e9finition%20EMR&lang=<?php echo $lang;?>#4.0">4.0 &laquo;&nbsp;Classification des patients par statut d&rsquo;activit&eacute;&nbsp;&raquo;</A>
ci-dessus pour les d&eacute;finitions des statuts de patient)</I>.
        Ce rapport peut &ecirc;tre
utilis&eacute; au sein des &eacute;tablissements afin de v&eacute;rifier
les registres papier de laboratoire (cahiers) par rapport aux donn&eacute;es
&eacute;lectroniques du patient, et aider le personnel de laboratoire
&agrave; mettre &agrave; jour le syst&egrave;me EMR avec les
prescriptions d&rsquo;analyses et les analyses effectu&eacute;es pour
chaque patient. Ces donn&eacute;es proviennent de toutes les fiches
de laboratoire compl&eacute;t&eacute;es au sein de l&rsquo;&eacute;tablissement
m&eacute;dical s&eacute;lectionn&eacute;.
	</FONT></P>
